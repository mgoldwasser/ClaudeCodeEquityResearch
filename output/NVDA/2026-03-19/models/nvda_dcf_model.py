def calc_wacc(
    rf: float = RISK_FREE_RATE,
    erp: float = EQUITY_RISK_PREMIUM,
    beta: float = BETA,
    specific: float = SPECIFIC_RISK_PREMIUM,
    debt_cost: float = COST_OF_DEBT,
    tax: float = TAX_RATE_BASE,
    d_weight: float = DEBT_WEIGHT,
    e_weight: float = EQUITY_WEIGHT,
) -> Dict:
    """
    Calculate Weighted Average Cost of Capital using CAPM for equity.

    Args:
        rf: Risk-free rate (decimal)
        erp: Equity risk premium (decimal)
        beta: Levered equity beta
        specific: Company-specific risk premium (decimal)
        debt_cost: Pre-tax cost of debt (decimal)
        tax: Effective tax rate (decimal)
        d_weight: Debt weight in capital structure (D/(D+E))
        e_weight: Equity weight in capital structure (E/(D+E))

    Returns:
        Dict with wacc, cost_of_equity, after_tax_cost_of_debt, components
    """
    if not (0 < rf < 1):
        raise ValueError(f"Risk-free rate {rf} must be between 0 and 1")
    if beta <= 0:
        raise ValueError(f"Beta {beta} must be positive")
    if not abs(d_weight + e_weight - 1.0) < 1e-6:
        raise ValueError("Debt and equity weights must sum to 1.0")

    cost_of_equity = rf + beta * erp + specific
    after_tax_cost_of_debt = debt_cost * (1 - tax)
    wacc = e_weight * cost_of_equity + d_weight * after_tax_cost_of_debt

    return {
        "wacc": round(wacc, 6),
        "cost_of_equity": round(cost_of_equity, 6),
        "after_tax_cost_of_debt": round(after_tax_cost_of_debt, 6),
        "components": {
            "risk_free_rate": rf,
            "equity_risk_premium": erp,
            "beta": beta,
            "specific_risk_premium": specific,
            "pre_tax_cost_of_debt": debt_cost,
            "tax_rate": tax,
            "debt_weight": d_weight,
            "equity_weight": e_weight,
        },
    }


def build_revenue(base_revenue: float, growth_rates: List[float], years: List[int]) -> Dict:
    """
    Project revenue from base using annual growth rates.

    Args:
        base_revenue: Starting revenue (millions USD)
        growth_rates: List of annual growth rates (decimals) for each year
        years: List of forecast year labels

    Returns:
        Dict mapping year to projected revenue
    """
    if len(growth_rates) != len(years):
        raise ValueError("Length of growth_rates must equal length of years")

    revenue = {}
    prev = base_revenue
    for yr, gr in zip(years, growth_rates):
        rev = prev * (1 + gr)
        revenue[yr] = round(rev, 1)
        prev = rev
    return revenue


def build_income_statement(
    revenue: Dict[int, float],
    gm_rate: float,
    opex_ratio: float,
    tax_rate: float,
) -> Dict[int, Dict]:
    """
    Build projected income statement from revenue and margin assumptions.

    Args:
        revenue: Dict of year → revenue (millions)
        gm_rate: Gross margin as decimal
        opex_ratio: Operating expenses as % of revenue
        tax_rate: Effective income tax rate as decimal

    Returns:
        Dict of year → income statement components
    """
    statements = {}
    for yr, rev in revenue.items():
        gross_profit = rev * gm_rate
        ebitda = gross_profit - rev * opex_ratio
        nopat = ebitda * (1 - tax_rate)   # Net Operating Profit After Tax
        statements[yr] = {
            "revenue": round(rev, 1),
            "gross_profit": round(gross_profit, 1),
            "ebitda": round(ebitda, 1),
            "nopat": round(nopat, 1),
        }
    return statements


def build_fcf(
    income_statements: Dict[int, Dict],
    capex_ratio: float,
    wc_change_ratio: float,
) -> Dict[int, float]:
    """
    Derive unlevered Free Cash Flow from income statement + capex/WC assumptions.

    Args:
        income_statements: Output from build_income_statement()
        capex_ratio: CapEx as % of revenue (decimal)
        wc_change_ratio: Working capital change as % of revenue (decimal, positive = use)

    Returns:
        Dict of year → unlevered FCF (millions)
    """
    fcfs = {}
    for yr, stmt in income_statements.items():
        rev = stmt["revenue"]
        capex = rev * capex_ratio
        wc_change = rev * wc_change_ratio
        fcf = stmt["nopat"] - capex - wc_change
        fcfs[yr] = round(fcf, 1)
    return fcfs


def calc_terminal_value(
    final_fcf: float,
    final_ebitda: float,
    wacc: float,
    tgr: float,
    exit_multiple: float,
) -> Dict:
    """
    Calculate terminal value using both Gordon Growth and EV/EBITDA exit multiple.
    Average the two methods for conservatism.

    Args:
        final_fcf: Final forecast year unlevered FCF (millions)
        final_ebitda: Final forecast year EBITDA (millions)
        wacc: WACC as decimal
        tgr: Terminal growth rate as decimal
        exit_multiple: EV/EBITDA exit multiple for final year

    Returns:
        Dict with gordon_tv, multiple_tv, blended_tv
    """
    if wacc <= tgr:
        raise ValueError(f"WACC ({wacc:.1%}) must exceed TGR ({tgr:.1%}) for Gordon Growth")

    gordon_tv = final_fcf * (1 + tgr) / (wacc - tgr)
    multiple_tv = final_ebitda * exit_multiple
    blended_tv = (gordon_tv + multiple_tv) / 2

    return {
        "gordon_growth_tv": round(gordon_tv, 1),
        "exit_multiple_tv": round(multiple_tv, 1),
        "blended_tv": round(blended_tv, 1),
        "wacc": wacc,
        "tgr": tgr,
        "exit_multiple": exit_multiple,
    }


def calc_enterprise_value(
    fcfs: Dict[int, float],
    terminal_value: float,
    wacc: float,
    base_year: int = 2026,
) -> Dict:
    """
    Discount FCFs and terminal value to present value.

    Args:
        fcfs: Dict of year → unlevered FCF (millions)
        terminal_value: Terminal value in final forecast year dollars (millions)
        wacc: WACC as decimal
        base_year: Valuation date year (discounting from end of each forecast year)

    Returns:
        Dict with pv_fcfs, pv_tv, enterprise_value, tv_pct_of_ev
    """
    pv_fcfs = {}
    sorted_years = sorted(fcfs.keys())
    for yr in sorted_years:
        t = yr - base_year
        discount_factor = 1 / (1 + wacc) ** t
        pv_fcfs[yr] = round(fcfs[yr] * discount_factor, 1)

    final_year = sorted_years[-1]
    t_final = final_year - base_year
    pv_tv = round(terminal_value / (1 + wacc) ** t_final, 1)

    sum_pv_fcf = sum(pv_fcfs.values())
    ev = sum_pv_fcf + pv_tv
    tv_pct = pv_tv / ev if ev > 0 else 0

    return {
        "pv_fcfs_by_year": pv_fcfs,
        "sum_pv_fcfs": round(sum_pv_fcf, 1),
        "pv_terminal_value": pv_tv,
        "enterprise_value": round(ev, 1),
        "tv_pct_of_ev": round(tv_pct, 4),
        "tv_warning": tv_pct > 0.50,
    }


def equity_bridge(
    enterprise_value: float,
    net_debt: float,
    shares: float,
) -> Dict:
    """
    Convert EV to equity value per share.

    Args:
        enterprise_value: EV in millions
        net_debt: Net debt in millions (negative = net cash)
        shares: Diluted shares outstanding in millions

    Returns:
        Dict with equity_value, price_per_share
    """
    if shares <= 0:
        raise ValueError("Shares must be positive")
    equity_value = enterprise_value - net_debt   # subtract debt, add back cash
    price_per_share = equity_value / shares
    return {
        "equity_value": round(equity_value, 1),
        "price_per_share": round(price_per_share, 2),
    }


def sensitivity_table(
    base_revenue: float,
    base_growth_rates: List[float],
    base_gm: float,
    base_opex: float,
    base_tax: float,
    base_capex: float,
    base_wc: float,
    base_tgr: float,
    base_exit_multiple: float,
    net_debt: float,
    shares: float,
    wacc_range: List[float],
    tgr_range: List[float],
) -> Dict:
    """
    Generate 2-D sensitivity table: WACC (rows) x Terminal Growth Rate (cols).

    Args:
        (base_* parameters): Base case assumptions
        wacc_range: List of WACC values to test
        tgr_range: List of TGR values to test

    Returns:
        Dict with header rows/cols and price_grid matrix
    """
    grid = []
    for wacc_val in wacc_range:
        row = []
        for tgr_val in tgr_range:
            try:
                rev = build_revenue(base_revenue, base_growth_rates, FORECAST_YEARS)
                stmts = build_income_statement(rev, base_gm, base_opex, base_tax)
                fcfs = build_fcf(stmts, base_capex, base_wc)
                final_yr = max(stmts.keys())
                tv_dict = calc_terminal_value(
                    fcfs[final_yr], stmts[final_yr]["ebitda"],
                    wacc_val, tgr_val, base_exit_multiple
                )
                ev_dict = calc_enterprise_value(fcfs, tv_dict["blended_tv"], wacc_val)
                bridge = equity_bridge(ev_dict["enterprise_value"], net_debt, shares)
                row.append(round(bridge["price_per_share"], 1))
            except ValueError:
                row.append(None)
        grid.append(row)

    return {
        "wacc_range": [round(w, 4) for w in wacc_range],
        "tgr_range": [round(t, 4) for t in tgr_range],
        "price_grid": grid,
        "description": "Implied price per share; rows=WACC, cols=TGR",
    }


def run_scenarios(scenario_configs: Dict) -> Dict:
    """
    Execute DCF for all defined scenarios and compute probability-weighted EV.

    Args:
        scenario_configs: Dict of scenario_name → parameter dict

    Returns:
        Dict of scenario results + probability-weighted price target
    """
    results = {}
    weighted_price = 0.0
    total_weight = 0.0

    for name, cfg in scenario_configs.items():
        wacc_dict = calc_wacc(tax=cfg["tax_rate"])
        wacc = wacc_dict["wacc"]

        rev = build_revenue(FY2026_BASE_REVENUE, cfg["growth_rates"], FORECAST_YEARS)
        stmts = build_income_statement(rev, cfg["gm_rate"], cfg["opex_ratio"], cfg["tax_rate"])
        fcfs = build_fcf(stmts, cfg["capex_ratio"], WC_CHANGE_RATIO)

        final_yr = max(stmts.keys())
        tv_dict = calc_terminal_value(
            fcfs[final_yr],
            stmts[final_yr]["ebitda"],
            wacc,
            cfg["tgr"],
            cfg["exit_multiple"],
        )
        ev_dict = calc_enterprise_value(fcfs, tv_dict["blended_tv"], wacc)
        bridge = equity_bridge(ev_dict["enterprise_value"], NET_DEBT, DILUTED_SHARES)

        prob = cfg["probability"]
        weighted_price += bridge["price_per_share"] * prob
        total_weight += prob

        results[name] = {
            "probability": prob,
            "revenue_projections": rev,
            "ebitda_projections": {yr: stmts[yr]["ebitda"] for yr in FORECAST_YEARS},
            "fcf_projections": fcfs,
            "wacc": wacc,
            "terminal_value": tv_dict,
            "enterprise_value": ev_dict["enterprise_value"],
            "tv_pct_of_ev": ev_dict["tv_pct_of_ev"],
            "tv_warning": ev_dict["tv_warning"],
            "equity_value": bridge["equity_value"],
            "price_per_share": bridge["price_per_share"],
        }

    if abs(total_weight - 1.0) > 0.01:
        raise ValueError(f"Scenario probabilities sum to {total_weight:.2f}, must sum to 1.0")

    results["probability_weighted_price"] = round(weighted_price, 2)
    return results


