def refinancing_analysis(
    instruments: List[Dict],
    current_rates: Dict[int, float],
    new_rate: float,
) -> Dict:
    """
    Model refinancing impact on interest expense as debt matures.

    Args:
        instruments: Debt instruments with amount, rate, maturity
        current_rates: Dict of maturity_year → current weighted rate
        new_rate: Assumed refinancing rate

    Returns:
        Dict with annual interest expense before and after refinancing
    """
    current_annual_interest = sum(d["amount"] * d["rate"] for d in instruments)
    new_annual_interest = sum(d["amount"] for d in instruments) * new_rate

    delta = new_annual_interest - current_annual_interest

    return {
        "current_annual_interest_expense": round(current_annual_interest, 1),
        "refinancing_rate": new_rate,
        "new_annual_interest_expense": round(new_annual_interest, 1),
        "annual_interest_change": round(delta, 1),
        "pct_change": round(delta / current_annual_interest, 4) if current_annual_interest > 0 else None,
        "note": (
            f"Refinancing at {new_rate:.1%} from weighted {sum(d['amount']*d['rate'] for d in instruments)/sum(d['amount'] for d in instruments):.1%} "
            f"increases annual interest by ${delta:+.1f}M — immaterial vs ${FCF:,.0f}M FCF"
        ),
    }


