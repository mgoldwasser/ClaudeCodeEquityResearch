def build_capital_structure(instruments: List[Dict], cash: float) -> Dict:
    """
    Summarize capital structure from debt instruments and cash position.

    Args:
        instruments: List of debt instrument dicts (amount, rate, type, maturity)
        cash: Total cash and liquid securities ($M)

    Returns:
        Dict with total_debt, cash, net_debt, weighted_avg_rate, structure_summary
    """
    total_debt = sum(d["amount"] for d in instruments)
    weighted_rate = (
        sum(d["amount"] * d["rate"] for d in instruments) / total_debt
        if total_debt > 0 else 0
    )
    annual_interest_expense = sum(d["amount"] * d["rate"] for d in instruments)
    net_position = cash - total_debt   # positive = net cash

    return {
        "total_debt": total_debt,
        "cash_and_securities": cash,
        "net_cash_position": net_position,
        "weighted_avg_interest_rate": round(weighted_rate, 4),
        "estimated_annual_interest": round(annual_interest_expense, 1),
        "instruments": instruments,
        "note": "[DATA GAP: Actual maturity schedule requires 10-K Note 12+]",
    }


def maturity_profile(instruments: List[Dict]) -> Dict:
    """
    Build debt maturity profile by year.

    Args:
        instruments: List of debt instrument dicts

    Returns:
        Dict of year → maturing debt ($M)
    """
    profile = {}
    for inst in instruments:
        yr = inst["maturity_year"]
        profile[yr] = profile.get(yr, 0) + inst["amount"]

    # Fill years with no maturities
