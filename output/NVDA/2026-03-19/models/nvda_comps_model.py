def safe_div(numerator: Optional[float], denominator: Optional[float]) -> Optional[float]:
    """Return numerator/denominator or None if either is None/zero/negative denominator."""
    if numerator is None or denominator is None:
        return None
    if denominator <= 0:
        return None
    return numerator / denominator


def calc_multiples(company: Dict, ev: float, market_cap: float) -> Dict:
    """
    Calculate standard valuation multiples for a company.

    Args:
        company: Company data dict with financial fields
        ev: Enterprise value in $M
        market_cap: Market cap in $M (for P/E, PEG)

    Returns:
        Dict with ev_revenue, ev_ebitda, p_e, ev_fcf, peg, p_fcf multiples
    """
    rev = company.get("revenue_ttm")
    ebitda = company.get("ebitda_ttm")
    net_income = company.get("net_income_ttm")
    fcf = company.get("fcf_ttm")
    eps = company.get("eps_ttm")
    fwd_growth = company.get("revenue_growth_fwd")

    ev_revenue = safe_div(ev, rev)
    ev_ebitda = safe_div(ev, ebitda)
    # P/E: use market_cap / net_income (or price/EPS)
    p_e = safe_div(market_cap, net_income) if net_income and net_income > 0 else None
    ev_fcf = safe_div(ev, fcf) if fcf and fcf > 0 else None
    # PEG = P/E divided by forward revenue growth rate (as percentage)
    peg = safe_div(p_e, (fwd_growth * 100)) if p_e and fwd_growth and fwd_growth > 0 else None

    return {
        "ev_revenue": round(ev_revenue, 1) if ev_revenue else None,
        "ev_ebitda": round(ev_ebitda, 1) if ev_ebitda else None,
        "p_e": round(p_e, 1) if p_e else None,
        "ev_fcf": round(ev_fcf, 1) if ev_fcf else None,
        "peg_ratio": round(peg, 2) if peg else None,
        "gross_margin": round(company.get("gross_margin", 0) * 100, 1),
        "ebitda_margin": round(company.get("ebitda_margin", 0) * 100, 1),
        "revenue_growth_1yr": round(company.get("revenue_growth_1yr", 0) * 100, 1),
        "revenue_growth_fwd": round(company.get("revenue_growth_fwd", 0) * 100, 1),
    }


def calc_statistics(values: List[Optional[float]]) -> Dict:
    """
    Calculate summary statistics for a list of multiples.

    Args:
        values: List of floats (None values are excluded)

    Returns:
        Dict with mean, median, std_dev, min, max, count
    """
    clean = [v for v in values if v is not None and v > 0]
    if not clean:
        return {"mean": None, "median": None, "std_dev": None, "min": None, "max": None, "count": 0}

    n = len(clean)
    mean = sum(clean) / n
    sorted_vals = sorted(clean)
    mid = n // 2
    median = sorted_vals[mid] if n % 2 == 1 else (sorted_vals[mid - 1] + sorted_vals[mid]) / 2
    variance = sum((x - mean) ** 2 for x in clean) / n if n > 1 else 0
    std_dev = math.sqrt(variance)
    p25_idx = max(0, int(0.25 * n) - 1)
    p75_idx = min(n - 1, int(0.75 * n))

    return {
        "mean": round(mean, 2),
        "median": round(median, 2),
        "std_dev": round(std_dev, 2),
        "min": round(sorted_vals[0], 2),
        "max": round(sorted_vals[-1], 2),
        "p25": round(sorted_vals[p25_idx], 2),
        "p75": round(sorted_vals[p75_idx], 2),
        "count": n,
    }


def calc_z_scores(subject_multiples: Dict, comp_multiples_list: List[Dict]) -> Dict:
    """
    Calculate z-scores for subject company vs comp set on each multiple.

    Args:
        subject_multiples: Multiples dict for subject (NVDA)
        comp_multiples_list: List of multiples dicts for comp set

    Returns:
        Dict of multiple_name → z_score
    """
    z_scores = {}
    multiple_keys = ["ev_revenue", "ev_ebitda", "p_e", "ev_fcf"]

    for key in multiple_keys:
        subject_val = subject_multiples.get(key)
        comp_vals = [m.get(key) for m in comp_multiples_list]
        stats = calc_statistics(comp_vals)

        if subject_val is None or stats["std_dev"] is None or stats["std_dev"] == 0:
            z_scores[key] = None
        else:
            z = (subject_val - stats["mean"]) / stats["std_dev"]
            z_scores[key] = round(z, 2)

    return z_scores


def implied_valuation(
    subject_financials: Dict,
    comp_stats: Dict,
    metric: str,
    net_debt: float,
    shares: float,
) -> Dict:
    """
    Derive implied equity value per share from comp set multiples.

    Args:
        subject_financials: Subject company financial data
        comp_stats: Statistics dict from calc_statistics()
        metric: Metric name (e.g., 'ev_revenue', 'ev_ebitda')
        net_debt: Net debt in $M (negative = net cash)
        shares: Diluted shares in millions

    Returns:
        Dict with p25_implied, median_implied, p75_implied price per share
    """
    if metric == "ev_revenue":
        base = subject_financials.get("revenue_ttm")
    elif metric == "ev_ebitda":
        base = subject_financials.get("ebitda_ttm")
    elif metric == "ev_fcf":
        base = subject_financials.get("fcf_ttm")
    else:
        return {}

    if base is None or base <= 0:
        return {}

    results = {}
    for pct_key in ["p25", "median", "p75"]:
        multiple = comp_stats.get(pct_key)
        if multiple is None:
            results[f"{pct_key}_price"] = None
            continue
        ev = base * multiple
        equity_val = ev - net_debt
        price = equity_val / shares
        results[f"{pct_key}_price"] = round(price, 2)

    return results


def flag_outliers(comp_multiples: List[Dict], threshold: float = 2.0) -> List[str]:
    """
    Flag comps where any key multiple is > threshold std deviations from mean.

    Args:
        comp_multiples: List of (ticker, multiples_dict) tuples
        threshold: Standard deviation cutoff

    Returns:
        List of outlier descriptions
    """
    flags = []
    for key in ["ev_revenue", "ev_ebitda", "p_e"]:
        vals = [(item[0], item[1].get(key)) for item in comp_multiples if item[1].get(key)]
        all_vals = [v for _, v in vals]
        stats = calc_statistics(all_vals)
        if stats["std_dev"] and stats["std_dev"] > 0:
            for ticker, val in vals:
                z = abs((val - stats["mean"]) / stats["std_dev"])
                if z > threshold:
                    flags.append(f"{ticker} {key}={val:.1f} (z={z:.1f})")
    return flags


def composite_rank(
    companies: List[Tuple[str, Dict, Dict]],
) -> List[Tuple[str, float]]:
    """
    Composite ranking on value + quality dimensions.
    Lower score = better value; higher growth/margins = higher quality.

    Args:
        companies: List of (ticker, financials, multiples) tuples

    Returns:
        List of (ticker, composite_score) sorted ascending (best first)
    """
    scores = []
    for ticker, fins, mults in companies:
        # Value score: lower EV/Revenue + lower P/E = better value
        val_score = (mults.get("ev_revenue") or 50) * 0.5 + (mults.get("p_e") or 100) * 0.02
        # Quality score: higher gross margin + higher growth = better quality
        qual_score = -(mults.get("gross_margin") or 0) * 0.5 - (mults.get("revenue_growth_fwd") or 0) * 2
        composite = val_score + qual_score
        scores.append((ticker, round(composite, 2)))

    scores.sort(key=lambda x: x[1])
    return scores


