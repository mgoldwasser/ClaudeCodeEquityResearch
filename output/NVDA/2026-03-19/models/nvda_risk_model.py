def monte_carlo_dcf(n_sims: int = N_SIMULATIONS) -> Dict:
    """
    Run Monte Carlo simulation over 5-year DCF with stochastic inputs.

    Samples from distributions for:
    - Annual revenue growth (log-normal)
    - Gross margin (normal, clipped 0.50-0.90)
    - WACC (normal, clipped 0.07-0.20)
    - Terminal growth rate (normal, clipped 0.01-0.06)
    - OpEx ratio (normal)
    - CapEx ratio (normal)
    - Exit multiple (normal, clipped 8-50)

    Args:
        n_sims: Number of Monte Carlo iterations

    Returns:
        Dict with price distribution, percentiles, VaR, CVaR
    """
    random.seed(RANDOM_SEED)
    prices = []

    for _ in range(n_sims):
        # Draw stochastic inputs
        wacc = max(0.07, min(0.20, _normal_sample(WACC_MEAN, WACC_STD)))
        gm = max(0.50, min(0.90, _normal_sample(GM_MEAN, GM_STD)))
        opex = max(0.07, min(0.20, _normal_sample(OPEX_MEAN, OPEX_STD)))
        capex = max(0.01, min(0.08, _normal_sample(CAPEX_MEAN, CAPEX_STD)))
        tgr = max(0.01, min(0.06, _normal_sample(TGR_MEAN, TGR_STD)))
        exit_mult = max(8.0, min(50.0, _normal_sample(EXIT_MULTIPLE_MEAN, EXIT_MULTIPLE_STD)))

        # Project revenue
        revenue = FY2026_REVENUE
        fcfs = []
        ebitda_final = 0.0
        fcf_final = 0.0

        for yr in range(FORECAST_YEARS):
            gr = _lognormal_sample(
                math.log(1 + REVENUE_GROWTH_MU_BASE) - 0.5 * REVENUE_GROWTH_SIGMA_BASE ** 2,
                REVENUE_GROWTH_SIGMA_BASE
            ) - 1.0
            gr = max(-0.40, min(1.50, gr))   # clip extreme values
            revenue = revenue * (1 + gr)
            ebitda = revenue * (gm - opex)
            nopat = ebitda * (1 - TAX_RATE)
            capex_abs = revenue * capex
            wc = revenue * 0.005
            fcf = nopat - capex_abs - wc
            fcfs.append(fcf)
            ebitda_final = ebitda
            fcf_final = fcf

        # Terminal value (blended)
        if wacc > tgr:
            tv_gordon = fcf_final * (1 + tgr) / (wacc - tgr)
        else:
            tv_gordon = fcf_final * 25   # fallback cap
        tv_multiple = ebitda_final * exit_mult
        tv = (tv_gordon + tv_multiple) / 2

        # Discount to PV
        pv_fcf = sum(fcfs[t] / (1 + wacc) ** (t + 1) for t in range(FORECAST_YEARS))
        pv_tv = tv / (1 + wacc) ** FORECAST_YEARS

        ev = pv_fcf + pv_tv
        equity_val = ev - NET_DEBT
        price = equity_val / DILUTED_SHARES
        prices.append(price)

    prices.sort()

    mean_price = _mean(prices)
    std_price = _std(prices)
    p5 = _percentile(prices, 5)
    p10 = _percentile(prices, 10)
    p25 = _percentile(prices, 25)
    p50 = _percentile(prices, 50)
    p75 = _percentile(prices, 75)
    p90 = _percentile(prices, 90)
    p95 = _percentile(prices, 95)

    # VaR and CVaR at 95% confidence (worst 5% scenarios)
    cutoff_95 = int(0.05 * len(prices))
    var_95 = p5   # 5th percentile price
    cvar_95 = _mean(prices[:cutoff_95]) if cutoff_95 > 0 else p5

    cutoff_99 = max(1, int(0.01 * len(prices)))
    var_99 = _percentile(prices, 1)
    cvar_99 = _mean(prices[:cutoff_99])

    # Distribution buckets for histogram
    min_p = min(prices)
    max_p = max(prices)
    n_buckets = 20
    bucket_size = (max_p - min_p) / n_buckets if max_p > min_p else 1
    histogram = {}
    for p in prices:
        bucket = int((p - min_p) / bucket_size)
        bucket = min(bucket, n_buckets - 1)
        label = f"${min_p + bucket * bucket_size:.0f}-${min_p + (bucket + 1) * bucket_size:.0f}"
        histogram[label] = histogram.get(label, 0) + 1

    return {
        "n_simulations": n_sims,
        "mean_price": round(mean_price, 2),
        "std_price": round(std_price, 2),
        "percentiles": {
            "p5": round(p5, 2),
            "p10": round(p10, 2),
            "p25": round(p25, 2),
            "p50": round(p50, 2),
            "p75": round(p75, 2),
            "p90": round(p90, 2),
            "p95": round(p95, 2),
        },
        "var_95_price": round(var_95, 2),
        "cvar_95_price": round(cvar_95, 2),
        "var_99_price": round(var_99, 2),
        "cvar_99_price": round(cvar_99, 2),
        "price_distribution_histogram": histogram,
    }


