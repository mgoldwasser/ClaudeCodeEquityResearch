def _mean(values: List[float]) -> float:
    """Arithmetic mean."""
    return sum(values) / len(values) if values else 0.0


def _std(values: List[float], ddof: int = 0) -> float:
    """Standard deviation."""
    if len(values) < 2:
        return 0.0
    m = _mean(values)
    var = sum((x - m) ** 2 for x in values) / (len(values) - ddof)
    return math.sqrt(var)


def _percentile(values: List[float], pct: float) -> float:
    """Compute percentile (0-100) of sorted list."""
    if not values:
        return 0.0
    sorted_vals = sorted(values)
    k = (len(sorted_vals) - 1) * pct / 100
    f = int(k)
    c = f + 1
    if c >= len(sorted_vals):
        return sorted_vals[-1]
    return sorted_vals[f] + (k - f) * (sorted_vals[c] - sorted_vals[f])


def _normal_sample(mu: float = 0.0, sigma: float = 1.0) -> float:
    """Box-Muller transform for normal random variable."""
    while True:
        u1 = random.random()
        u2 = random.random()
        if u1 > 0:
            break
    z0 = math.sqrt(-2.0 * math.log(u1)) * math.cos(2.0 * math.pi * u2)
    return mu + sigma * z0


def _lognormal_sample(mu: float, sigma: float) -> float:
    """Log-normal sample: X = exp(N(mu, sigma))."""
    return math.exp(_normal_sample(mu, sigma))

