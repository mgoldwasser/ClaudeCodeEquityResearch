def calc_var(
    expected_return: float = EXPECTED_ANNUAL_RETURN,
    volatility: float = ANNUAL_VOLATILITY,
    confidence: float = 0.95,
    horizon_years: float = 1.0,
) -> Dict:
    """
    Parametric VaR and CVaR on equity returns (normal distribution assumption).

    Args:
        expected_return: Expected annual return (decimal)
        volatility: Annual return volatility (decimal)
        confidence: Confidence level (0.95 = 95%)
        horizon_years: Holding period in years

    Returns:
        Dict with VaR, CVaR (expected shortfall), and key parameters
    """
    # Scale to horizon
    mu_h = expected_return * horizon_years
    sigma_h = volatility * math.sqrt(horizon_years)

    # Normal quantile approximation (z-score)
    # Precomputed: z(0.95) = 1.6449, z(0.99) = 2.3263
    z_table = {0.90: 1.2816, 0.95: 1.6449, 0.99: 2.3263}
    z = z_table.get(confidence, 1.6449)

    var_return = mu_h - z * sigma_h   # return at VaR threshold
    # CVaR (expected shortfall) for normal: mu - sigma * phi(z) / (1 - confidence)
    # phi(z) = standard normal PDF at z
    phi_z = math.exp(-0.5 * z * z) / math.sqrt(2 * math.pi)
    cvar_return = mu_h - sigma_h * phi_z / (1 - confidence)

    return {
        "confidence": confidence,
        "horizon_years": horizon_years,
        "expected_return": expected_return,
        "volatility": volatility,
        "var_return": round(var_return, 4),
        "cvar_return": round(cvar_return, 4),
        "var_description": f"With {confidence:.0%} confidence, annual return will not be worse than {var_return:.1%}",
        "cvar_description": f"Expected return given tail event beyond {confidence:.0%} VaR: {cvar_return:.1%}",
        "method": "Parametric (Normal distribution)",
    }


