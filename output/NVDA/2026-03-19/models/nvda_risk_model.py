def risk_adjusted_returns(
    expected_return: float = EXPECTED_ANNUAL_RETURN,
    volatility: float = ANNUAL_VOLATILITY,
    risk_free: float = RISK_FREE_RATE,
) -> Dict:
    """
    Calculate risk-adjusted return metrics.

    Args:
        expected_return: Expected annual return (decimal)
        volatility: Annual return volatility (decimal)
        risk_free: Risk-free rate (decimal)

    Returns:
        Dict with Sharpe, Sortino (approx), Kelly fraction
    """
    if volatility <= 0:
        raise ValueError("Volatility must be positive")

    sharpe = (expected_return - risk_free) / volatility

    # Sortino: approximate downside vol as volatility × 0.7 (assumes ~30% of moves are positive)
    downside_vol = volatility * 0.65   # [ASSUMPTION: rough approximation]
    sortino = (expected_return - risk_free) / downside_vol

    # Kelly fraction: f* = (mu - rf) / sigma^2
    kelly_full = (expected_return - risk_free) / (volatility ** 2)
    kelly_half = kelly_full / 2
    kelly_quarter = kelly_full / 4

    return {
        "expected_return": expected_return,
        "volatility": volatility,
        "risk_free": risk_free,
        "sharpe_ratio": round(sharpe, 3),
        "sortino_ratio_approx": round(sortino, 3),
        "kelly_full": round(kelly_full, 4),
        "kelly_half": round(kelly_half, 4),
        "kelly_quarter": round(kelly_quarter, 4),
        "practical_kelly": round(kelly_quarter, 4),   # Use quarter Kelly as practical bound
        "note": "Kelly assumes returns follow the expected return/volatility distribution; "
                "use quarter-Kelly as practical sizing due to parameter uncertainty",
    }


