def build_capital_structure(debt_instruments: List[Dict], cash_m: float) -> Dict:
    """
    Summarize the capital structure.

    Args:
        debt_instruments: List of debt instrument dicts.
        cash_m: Cash and investments ($M).

    Returns:
        Dict with total_debt, net_debt, weighted_avg_coupon, active instruments.
    """
    active = [d for d in debt_instruments if d["status"] == "active" and d["amount_M"] > 0]
    total_debt = sum(d["amount_M"] for d in active)
    if total_debt > 0:
        weighted_coupon = sum(d["amount_M"] * d["coupon_pct"] for d in active) / total_debt
    else:
        weighted_coupon = 0

    net_debt = total_debt - cash_m
    all_fixed = all(d["type"] == "fixed" for d in active)

    return {
        "total_debt_M": total_debt,
        "cash_M": cash_m,
        "net_debt_M": net_debt,
        "net_cash_position": net_debt < 0,
        "weighted_avg_coupon_pct": round(weighted_coupon, 3),
        "active_instruments": len(active),
        "all_fixed_rate": all_fixed,
        "instruments": [{
            "name": d["name"],
            "amount_M": d["amount_M"],
            "coupon_pct": d["coupon_pct"],
            "maturity": d["maturity_year"],
            "type": d["type"],
        } for d in active],
    }


def maturity_profile(debt_instruments: List[Dict]) -> Dict:
    """
    Build debt maturity profile by year.

    Args:
        debt_instruments: List of debt instrument dicts.

    Returns:
        Dict with 'by_year' mapping year -> amount, and 'weighted_avg_maturity'.
    """
    active = [d for d in debt_instruments if d["status"] == "active" and d["amount_M"] > 0]
    by_year = {}
    total = 0
    weighted_mat = 0
    current_year = 2026

    for d in active:
        year = d["maturity_year"]
        by_year[year] = by_year.get(year, 0) + d["amount_M"]
        total += d["amount_M"]
        years_to_mat = max(year - current_year, 0)
        weighted_mat += d["amount_M"] * years_to_mat

    wam = weighted_mat / total if total > 0 else 0

    # Bucket into near/medium/long term
    near = sum(v for y, v in by_year.items() if y <= current_year + 2)
    medium = sum(v for y, v in by_year.items() if current_year + 2 < y <= current_year + 5)
    long_term = sum(v for y, v in by_year.items() if y > current_year + 5)

    return {
        "by_year": dict(sorted(by_year.items())),
        "weighted_avg_maturity_years": round(wam, 1),
        "near_term_M": near,
        "medium_term_M": medium,
        "long_term_M": long_term,
    }


def leverage_ratios(total_debt_m: float, cash_m: float, ebitda_m: float,
                    equity_m: float, total_assets_m: float) -> Dict:
    """
    Calculate leverage ratios.

    Args:
        total_debt_m: Total debt ($M).
        cash_m: Cash ($M).
        ebitda_m: EBITDA ($M).
        equity_m: Total equity ($M).
        total_assets_m: Total assets ($M).

    Returns:
        Dict with various leverage ratios.
    """
    net_debt = total_debt_m - cash_m
    return {
        "gross_debt_to_ebitda": round(total_debt_m / ebitda_m, 2) if ebitda_m > 0 else None,
        "net_debt_to_ebitda": round(net_debt / ebitda_m, 2) if ebitda_m > 0 else None,
        "debt_to_equity": round(total_debt_m / equity_m, 3) if equity_m > 0 else None,
        "debt_to_assets": round(total_debt_m / total_assets_m, 3) if total_assets_m > 0 else None,
        "net_debt_M": round(net_debt, 0),
        "net_cash_position": net_debt < 0,
    }


def coverage_ratios(ebitda_m: float, ebit_m: float, interest_m: float,
                    fcf_m: float, total_debt_m: float) -> Dict:
    """
    Calculate interest and debt service coverage ratios.

    Args:
        ebitda_m: EBITDA ($M).
        ebit_m: EBIT ($M).
        interest_m: Interest expense ($M).
        fcf_m: Free cash flow ($M).
        total_debt_m: Total debt ($M).

    Returns:
        Dict with coverage ratios.
    """
    return {
        "ebitda_to_interest": round(ebitda_m / interest_m, 1) if interest_m > 0 else float('inf'),
        "ebit_to_interest": round(ebit_m / interest_m, 1) if interest_m > 0 else float('inf'),
        "fcf_to_debt": round(fcf_m / total_debt_m, 2) if total_debt_m > 0 else float('inf'),
        "fcf_to_interest": round(fcf_m / interest_m, 1) if interest_m > 0 else float('inf'),
        "debt_paydown_years": round(total_debt_m / fcf_m, 1) if fcf_m > 0 else float('inf'),
    }


def covenant_headroom(leverage: Dict, coverage: Dict,
                      max_leverage: float, min_coverage: float,
                      min_current: float, current_ratio: float) -> Dict:
    """
    Assess covenant compliance and headroom.

    Args:
        leverage: Leverage ratios dict.
        coverage: Coverage ratios dict.
        max_leverage: Covenant max leverage ratio.
        min_coverage: Covenant min interest coverage.
        min_current: Covenant min current ratio.
        current_ratio: Actual current ratio.

    Returns:
        Dict with compliance status and headroom for each covenant.
    """
    net_leverage = leverage["net_debt_to_ebitda"]
    int_coverage = coverage["ebitda_to_interest"]

    # Handle net cash position (negative leverage)
    lev_headroom = max_leverage - net_leverage if net_leverage is not None else max_leverage
    cov_headroom = int_coverage - min_coverage if int_coverage != float('inf') else float('inf')
    cur_headroom = current_ratio - min_current

    return {
        "leverage_covenant": {
            "threshold": max_leverage,
            "actual": net_leverage,
            "headroom": round(lev_headroom, 2),
            "compliant": lev_headroom > 0,
            "pct_headroom": round(lev_headroom / max_leverage * 100, 1) if max_leverage > 0 else 100,
        },
        "coverage_covenant": {
            "threshold": min_coverage,
            "actual": int_coverage,
            "headroom": round(cov_headroom, 1) if cov_headroom != float('inf') else "Infinite",
            "compliant": True,
        },
        "current_ratio_covenant": {
            "threshold": min_current,
            "actual": current_ratio,
            "headroom": round(cur_headroom, 2),
            "compliant": cur_headroom > 0,
        },
    }


def stress_test_covenants(base_ebitda: float, declines: List[float],
                          total_debt: float, interest: float, cash: float,
                          covenants: Dict) -> List[Dict]:
    """
    Stress test covenant compliance under EBITDA decline scenarios.

    Args:
        base_ebitda: Base EBITDA ($M).
        declines: List of EBITDA decline percentages (decimals, negative).
        total_debt: Total debt ($M).
        interest: Interest expense ($M).
        cash: Cash ($M).
        covenants: Dict with covenant thresholds.

    Returns:
        List of stress test results.
    """
    results = []
    for decline in declines:
        stressed_ebitda = base_ebitda * (1 + decline)
        if stressed_ebitda <= 0:
            stressed_ebitda = 1  # Avoid division by zero
        net_debt = total_debt - cash
        net_leverage = net_debt / stressed_ebitda
        int_coverage = stressed_ebitda / interest if interest > 0 else float('inf')

        lev_breach = net_leverage > covenants.get("max_leverage", 999)
        cov_breach = int_coverage < covenants.get("min_coverage", 0)

        results.append({
            "ebitda_decline_pct": round(decline * 100, 0),
            "stressed_ebitda_M": round(stressed_ebitda, 0),
            "net_leverage": round(net_leverage, 2),
            "interest_coverage": round(int_coverage, 1),
            "leverage_breach": lev_breach,
            "coverage_breach": cov_breach,
            "any_breach": lev_breach or cov_breach,
        })
    return results


def altman_z_score(working_capital: float, retained_earnings: float,
                   ebit: float, market_cap: float, total_debt: float,
                   sales: float, total_assets: float) -> Dict:
    """
    Calculate Altman Z-Score for bankruptcy risk assessment.

    Z = 1.2*X1 + 1.4*X2 + 3.3*X3 + 0.6*X4 + 1.0*X5

    Args:
        working_capital: Current assets - current liabilities ($M).
        retained_earnings: Retained earnings ($M).
        ebit: EBIT ($M).
        market_cap: Market capitalization ($M).
        total_debt: Total debt ($M).
        sales: Annual revenue ($M).
        total_assets: Total assets ($M).

    Returns:
        Dict with z_score, components, and risk assessment.
    """
    if total_assets <= 0:
        raise ValueError("Total assets must be positive")

    x1 = working_capital / total_assets
    x2 = retained_earnings / total_assets
    x3 = ebit / total_assets
    x4 = market_cap / total_debt if total_debt > 0 else 100  # Cap if no debt
    x5 = sales / total_assets

    z = 1.2 * x1 + 1.4 * x2 + 3.3 * x3 + 0.6 * x4 + 1.0 * x5

    if z > 2.99:
        zone = "Safe Zone"
        risk = "Low bankruptcy risk"
    elif z > 1.81:
        zone = "Grey Zone"
        risk = "Moderate risk — monitor closely"
    else:
        zone = "Distress Zone"
        risk = "High bankruptcy risk"

    return {
        "z_score": round(z, 2),
        "zone": zone,
        "risk_assessment": risk,
        "components": {
            "X1_wc_to_assets": round(x1, 4),
            "X2_re_to_assets": round(x2, 4),
            "X3_ebit_to_assets": round(x3, 4),
            "X4_mktcap_to_debt": round(x4, 2),
            "X5_sales_to_assets": round(x5, 4),
        },
    }


def debt_capacity(ebitda: float, cash: float, current_debt: float,
                  target_leverage: float, max_leverage: float,
                  min_coverage: float, cost_of_debt: float) -> Dict:
    """
    Calculate additional debt capacity.

    Args:
        ebitda: Current EBITDA ($M).
        cash: Cash ($M).
        current_debt: Current total debt ($M).
        target_leverage: Target Net Debt/EBITDA.
        max_leverage: Maximum Net Debt/EBITDA.
        min_coverage: Minimum EBITDA/Interest coverage.
        cost_of_debt: Assumed new debt cost (decimal).

    Returns:
        Dict with debt capacity at target and maximum leverage.
    """
    current_net_debt = current_debt - cash

    # Capacity based on leverage
    target_net_debt = ebitda * target_leverage
    max_net_debt = ebitda * max_leverage
    target_capacity = target_net_debt - current_net_debt
    max_capacity = max_net_debt - current_net_debt

    # Capacity based on coverage
    current_interest = current_debt * cost_of_debt
    max_interest = ebitda / min_coverage
    interest_headroom = max_interest - current_interest
    coverage_capacity = interest_headroom / cost_of_debt if cost_of_debt > 0 else float('inf')

    # Binding constraint
    binding_capacity = min(max_capacity, coverage_capacity)

    return {
        "current_net_debt_M": round(current_net_debt, 0),
        "target_leverage_capacity_M": round(max(target_capacity, 0), 0),
        "max_leverage_capacity_M": round(max(max_capacity, 0), 0),
        "coverage_based_capacity_M": round(max(coverage_capacity, 0), 0),
        "binding_constraint": "leverage" if max_capacity < coverage_capacity else "coverage",
        "binding_capacity_M": round(max(binding_capacity, 0), 0),
        "note": "AMD is in net cash position; substantial debt capacity available" if current_net_debt < 0 else "",
    }


def liquidity_assessment(cash: float, revolver_available: float,
                         fcf_annual: float, near_term_maturities: float,
                         capex: float) -> Dict:
    """
    Assess liquidity adequacy.

    Args:
        cash: Cash and investments ($M).
        revolver_available: Available credit facility ($M).
        fcf_annual: Annual FCF ($M).
        near_term_maturities: Debt maturing within 2 years ($M).
        capex: Annual capital expenditure ($M).

    Returns:
        Dict with liquidity sources, uses, and adequacy assessment.
    """
    total_liquidity = cash + revolver_available
    annual_uses = near_term_maturities / 2 + capex  # Annualize maturities
    coverage_years = total_liquidity / annual_uses if annual_uses > 0 else float('inf')
    fcf_coverage = fcf_annual / annual_uses if annual_uses > 0 else float('inf')

    if coverage_years > 5:
        assessment = "Excellent — more than 5 years of liquidity coverage"
    elif coverage_years > 3:
        assessment = "Strong — 3-5 years of coverage"
    elif coverage_years > 1:
        assessment = "Adequate — 1-3 years of coverage"
    else:
        assessment = "Tight — less than 1 year of coverage, monitor closely"

    return {
        "sources": {
            "cash_M": cash,
            "revolver_available_M": revolver_available,
            "annual_fcf_M": fcf_annual,
            "total_liquidity_M": total_liquidity,
        },
        "uses": {
            "near_term_maturities_M": near_term_maturities,
            "annual_capex_M": capex,
        },
        "coverage_years": round(coverage_years, 1) if coverage_years != float('inf') else "Infinite",
        "fcf_coverage_ratio": round(fcf_coverage, 1) if fcf_coverage != float('inf') else "Infinite",
        "assessment": assessment,
    }


