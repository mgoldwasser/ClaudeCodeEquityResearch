def project_shares(shares_dict, years=5, use_field="share_2025"):
    """Project market shares forward, respecting ceilings/floors and normalizing to 100%."""
    projections = {}

    for year in range(years + 1):
        year_label = 2025 + year
        raw_shares = {}

        for company, data in shares_dict.items():
            base = data[use_field]
            trend = data.get("share_trend_annual_pp", 0) / 100.0
            projected = base + trend * year

            # Apply ceiling/floor constraints
            if "ceiling" in data:
                projected = min(projected, data["ceiling"])
            if "floor" in data:
                projected = max(projected, data["floor"])

            projected = max(0, projected)
            raw_shares[company] = projected

        # Normalize to 100%
        total = sum(raw_shares.values())
        projections[year_label] = {k: round(v / total, 4) for k, v in raw_shares.items()}

    return projections


def compute_hhi(shares):
    """Compute Herfindahl-Hirschman Index from market shares."""
    return round(sum((s * 100) ** 2 for s in shares.values()))


