def calc_wacc(rf: float, erp: float, beta: float, size_prem: float,
              specific: float, debt_cost_pretax: float, tax: float,
              d_tc: float) -> Dict:
    """
    Calculate Weighted Average Cost of Capital.

    Args:
        rf: Risk-free rate (decimal)
        erp: Equity risk premium (decimal)
        beta: Levered beta
        size_prem: Size premium (decimal)
        specific: Company-specific risk premium (decimal)
        debt_cost_pretax: Pre-tax cost of debt (decimal)
        tax: Tax rate (decimal)
        d_tc: Debt / Total Capital ratio (decimal)

    Returns:
        Dict with 'wacc', 'cost_of_equity', 'cost_of_debt_aftertax', components.
    """
    cost_of_equity = rf + beta * erp + size_prem + specific
    cost_of_debt_at = debt_cost_pretax * (1 - tax)
    e_tc = 1 - d_tc
    wacc = e_tc * cost_of_equity + d_tc * cost_of_debt_at
    return {
        "wacc": wacc,
        "cost_of_equity": cost_of_equity,
        "cost_of_debt_aftertax": cost_of_debt_at,
        "equity_weight": e_tc,
        "debt_weight": d_tc,
        "components": {
            "risk_free": rf,
            "erp": erp,
            "beta": beta,
            "size_premium": size_prem,
            "specific_risk": specific,
        }
    }


def build_revenue(segments: Dict[str, float], growth_rates: Dict[str, List[float]],
                  years: int = 5) -> Dict:
    """
    Build segment-level revenue projection.

    Args:
        segments: Dict of segment name -> FY2025 revenue in $M
        growth_rates: Dict of segment name -> list of annual growth rates
        years: Projection horizon

    Returns:
        Dict with 'by_segment' (segment -> list of revenues), 'total' (list of total revenues).
    """
    if years < 1 or years > 10:
        raise ValueError(f"years must be 1-10, got {years}")
    by_segment = {}
    totals = [0.0] * years
    for seg, base_rev in segments.items():
        if seg not in growth_rates:
            raise ValueError(f"No growth rates for segment: {seg}")
        if len(growth_rates[seg]) < years:
            raise ValueError(f"Need {years} growth rates for {seg}, got {len(growth_rates[seg])}")
        rev_list = []
        prev = base_rev
        for y in range(years):
            rev = prev * (1 + growth_rates[seg][y])
            rev_list.append(round(rev, 1))
            totals[y] += rev
            prev = rev
        by_segment[seg] = rev_list
    totals = [round(t, 1) for t in totals]
    return {"by_segment": by_segment, "total": totals}


def build_income_statement(revenue_total: List[float], gross_margins: List[float],
                           opex_pcts: List[float], da_pct: float) -> Dict:
    """
    Build projected income statement from revenue to EBIT/EBITDA.

    Args:
        revenue_total: List of total revenue by year ($M)
        gross_margins: List of gross margin % by year (decimal)
        opex_pcts: List of OpEx as % of revenue by year (decimal)
        da_pct: D&A as % of revenue (decimal)

    Returns:
        Dict with 'gross_profit', 'opex', 'ebit', 'ebitda', 'da' lists.
    """
    n = len(revenue_total)
    gp = [round(revenue_total[i] * gross_margins[i], 1) for i in range(n)]
    opex = [round(revenue_total[i] * opex_pcts[i], 1) for i in range(n)]
    ebit = [round(gp[i] - opex[i], 1) for i in range(n)]
    da = [round(revenue_total[i] * da_pct, 1) for i in range(n)]
    ebitda = [round(ebit[i] + da[i], 1) for i in range(n)]
    return {
        "gross_profit": gp,
        "opex": opex,
        "ebit": ebit,
        "ebitda": ebitda,
        "da": da,
    }


def build_fcf(ebitda: List[float], tax_rate: float, capex_pcts: List[float],
              revenue: List[float], wc_pct: float, prev_revenue: float) -> Dict:
    """
    Build Free Cash Flow bridge from EBITDA.

    Args:
        ebitda: List of EBITDA by year ($M)
        tax_rate: Effective tax rate (decimal)
        capex_pcts: CapEx as % of revenue by year (decimal)
        revenue: Revenue by year ($M)
        wc_pct: Working capital change as % of revenue change (decimal)
        prev_revenue: Prior year revenue for first year WC calc ($M)

    Returns:
        Dict with 'fcf', 'taxes', 'capex', 'wc_change' lists.
    """
    n = len(ebitda)
    taxes = [round(ebitda[i] * tax_rate, 1) for i in range(n)]
    capex = [round(revenue[i] * capex_pcts[i], 1) for i in range(n)]
    wc = []
    for i in range(n):
        prev_rev = prev_revenue if i == 0 else revenue[i - 1]
        delta_rev = revenue[i] - prev_rev
        wc.append(round(delta_rev * wc_pct, 1))
    fcf = [round(ebitda[i] - taxes[i] - capex[i] - wc[i], 1) for i in range(n)]
    return {
        "fcf": fcf,
        "taxes": taxes,
        "capex": capex,
        "wc_change": wc,
    }


def calc_terminal_value(final_fcf: float, wacc: float, tgr: float,
                        final_ebitda: float, exit_multiple: float) -> Dict:
    """
    Calculate terminal value using both perpetuity growth and exit multiple methods.

    Args:
        final_fcf: Final year FCF ($M)
        wacc: WACC (decimal)
        tgr: Terminal growth rate (decimal)
        final_ebitda: Final year EBITDA ($M)
        exit_multiple: Exit EV/EBITDA multiple

    Returns:
        Dict with 'perpetuity_tv', 'exit_multiple_tv', 'blended_tv' (50/50 average).
    """
    if wacc <= tgr:
        raise ValueError(f"WACC ({wacc:.2%}) must exceed TGR ({tgr:.2%})")
    perp_tv = final_fcf * (1 + tgr) / (wacc - tgr)
    exit_tv = final_ebitda * exit_multiple
    blended = (perp_tv + exit_tv) / 2
    return {
        "perpetuity_tv": round(perp_tv, 0),
        "exit_multiple_tv": round(exit_tv, 0),
        "blended_tv": round(blended, 0),
    }


def calc_enterprise_value(fcfs: List[float], terminal_value: float,
                          wacc: float) -> Dict:
    """
    Calculate enterprise value as PV of FCFs + PV of terminal value.

    Args:
        fcfs: List of projected FCFs ($M)
        terminal_value: Terminal value ($M)
        wacc: WACC (decimal)

    Returns:
        Dict with 'pv_fcfs', 'pv_tv', 'enterprise_value', 'tv_pct_of_ev'.
    """
    pv_fcfs = sum(fcf / (1 + wacc) ** (i + 1) for i, fcf in enumerate(fcfs))
    n = len(fcfs)
    pv_tv = terminal_value / (1 + wacc) ** n
    ev = pv_fcfs + pv_tv
    tv_pct = pv_tv / ev if ev > 0 else 0
    return {
        "pv_fcfs": round(pv_fcfs, 0),
        "pv_tv": round(pv_tv, 0),
        "enterprise_value": round(ev, 0),
        "tv_pct_of_ev": round(tv_pct, 4),
    }


def equity_bridge(ev_m: float, net_cash_m: float, shares_m: float,
                  warrant_shares_m: float, warrant_dilution_pct: float) -> Dict:
    """
    Bridge from enterprise value to equity value per share.

    Args:
        ev_m: Enterprise value ($M)
        net_cash_m: Net cash ($M, positive = cash > debt)
        shares_m: Basic shares outstanding (millions)
        warrant_shares_m: Potential warrant shares (millions)
        warrant_dilution_pct: Fraction of warrants assumed to vest (0-1)

    Returns:
        Dict with 'equity_value', 'diluted_shares', 'price_per_share'.
    """
    equity_value = ev_m + net_cash_m  # Add net cash (positive for net cash companies)
    diluted_shares = shares_m + warrant_shares_m * warrant_dilution_pct
    price = equity_value / diluted_shares if diluted_shares > 0 else 0
    return {
        "equity_value_M": round(equity_value, 0),
        "diluted_shares_M": round(diluted_shares, 1),
        "price_per_share": round(price, 2),
    }


def sensitivity_table(base_wacc: float, base_tgr: float, base_ev_fn,
                      wacc_range: List[float], tgr_range: List[float]) -> List[List]:
    """
    Generate WACC x Terminal Growth Rate sensitivity table.

    Args:
        base_wacc: Base WACC for reference
        base_tgr: Base TGR for reference
        base_ev_fn: Callable(wacc, tgr) -> price_per_share
        wacc_range: List of WACC values to test
        tgr_range: List of TGR values to test

    Returns:
        2D list with headers: first row = TGR values, first col = WACC values.
    """
    table = [["WACC \\ TGR"] + [f"{t:.1%}" for t in tgr_range]]
    for w in wacc_range:
        row = [f"{w:.1%}"]
        for t in tgr_range:
            try:
                price = base_ev_fn(w, t)
                row.append(f"${price:.0f}")
            except (ValueError, ZeroDivisionError):
                row.append("N/A")
        table.append(row)
    return table


def run_scenario(scenario_name: str, segments: Dict[str, float],
                 growth_rates: Dict[str, List[float]], gross_margins: List[float],
                 opex_pcts: List[float], capex_pcts: List[float],
                 wacc: float, tgr: float, exit_multiple: float,
                 tax_rate: float, da_pct: float, wc_pct: float,
                 prev_revenue: float, net_cash_m: float,
                 shares_m: float, warrant_shares_m: float,
                 warrant_dilution: float) -> Dict:
    """
    Run a complete DCF scenario end-to-end.

    Returns:
        Complete scenario results dict.
    """
    rev = build_revenue(segments, growth_rates)
    inc = build_income_statement(rev["total"], gross_margins, opex_pcts, da_pct)
    fcf = build_fcf(inc["ebitda"], tax_rate, capex_pcts, rev["total"], wc_pct, prev_revenue)
    tv = calc_terminal_value(fcf["fcf"][-1], wacc, tgr, inc["ebitda"][-1], exit_multiple)
    ev = calc_enterprise_value(fcf["fcf"], tv["blended_tv"], wacc)
    eq = equity_bridge(ev["enterprise_value"], net_cash_m * 1000, shares_m,
                       warrant_shares_m, warrant_dilution)
    return {
        "scenario": scenario_name,
        "revenue": rev,
        "income_statement": inc,
        "fcf_bridge": fcf,
        "terminal_value": tv,
        "enterprise_value": ev,
        "equity_bridge": eq,
        "implied_price": eq["price_per_share"],
    }


