def calculate_cost_of_equity(
    risk_free_rate: float,
    beta: float,
    market_risk_premium: float,
    size_premium: float = 0.0,
    specific_risk: float = 0.0
) -> float:
    """
    Calculate cost of equity using CAPM + adjustments.

    Args:
        risk_free_rate: Risk-free rate (e.g., 0.045)
        beta: Stock beta relative to market
        market_risk_premium: Expected market risk premium (e.g., 0.055)
        size_premium: Additional premium for company size (usually 0 for mega-cap)
        specific_risk: Company-specific risk adjustment (e.g., concentration risk)

    Returns:
        Cost of equity as decimal (e.g., 0.13475)
    """
    capm_component = risk_free_rate + (beta * market_risk_premium)
    return capm_component + size_premium + specific_risk


def calculate_wacc(
    cost_of_equity: float,
    cost_of_debt_pretax: float,
    debt_to_value: float,
    tax_rate: float
) -> float:
    """
    Calculate Weighted Average Cost of Capital.

    Args:
        cost_of_equity: Cost of equity (e.g., 0.13475)
        cost_of_debt_pretax: Pre-tax cost of debt (e.g., 0.035)
        debt_to_value: Debt / (Debt + Equity) ratio (e.g., 0.05)
        tax_rate: Effective tax rate (e.g., 0.15)

    Returns:
        WACC as decimal (e.g., 0.129)
    """
    equity_to_value = 1.0 - debt_to_value
    debt_cost_after_tax = cost_of_debt_pretax * (1.0 - tax_rate)
    wacc = (equity_to_value * cost_of_equity) + (debt_to_value * debt_cost_after_tax)
    return wacc


def project_financials(
    starting_revenue: float,
    growth_rates: List[float],
    gross_margins: List[float],
    opex_pcts: List[float],
    tax_rate: float,
    capex_pcts: List[float],
    nwc_pct_increase: float,
    years: int = 5
) -> Dict:
    """
    Project 5-year income statement and free cash flow.

    Args:
        starting_revenue: FY2026 revenue in $M
        growth_rates: List of 5 YoY growth rates
        gross_margins: List of 5 gross margin percentages
        opex_pcts: List of 5 OpEx as % revenue
        tax_rate: Effective tax rate
        capex_pcts: List of 5 CapEx as % revenue
        nwc_pct_increase: NWC change as % of revenue increase
        years: Number of projection years (default 5)

    Returns:
        Dict with keys: 'revenue', 'gross_profit', 'ebitda', 'ebit', 'nopat', 'fcf', 'details'
    """
    financials = {
        'year': [],
        'revenue': [],
        'revenue_growth': [],
        'gross_profit': [],
        'gross_margin': [],
        'opex': [],
        'ebitda': [],
        'ebitda_margin': [],
        'ebit': [],
        'tax': [],
        'nopat': [],
        'capex': [],
        'nwc_change': [],
        'fcf': [],
    }

    prev_revenue = starting_revenue
    prev_nwc = starting_revenue * 0.15  # Assume 15% of revenue as baseline NWC

    for year in range(1, years + 1):
        # Revenue projection
        growth = growth_rates[year - 1]
        revenue = prev_revenue * (1.0 + growth)

        # Gross profit
        gross_margin = gross_margins[year - 1]
        gross_profit = revenue * gross_margin

        # Operating expenses
        opex_pct = opex_pcts[year - 1]
        opex = revenue * opex_pct

        # EBITDA (simplified as GP - OpEx, assuming minimal D&A)
        ebitda = gross_profit - opex
        ebitda_margin = ebitda / revenue if revenue > 0 else 0

        # EBIT = EBITDA - D&A (simplified: assume minimal D&A for fabless company)
        ebit = ebitda

        # Tax on EBIT
        tax = ebit * tax_rate

        # NOPAT (Net Operating Profit After Tax)
        nopat = ebit * (1.0 - tax_rate)

        # CapEx
        capex = revenue * capex_pcts[year - 1]

        # Change in Net Working Capital
        current_nwc = revenue * 0.15  # Assume 15% of revenue as NWC
        nwc_change = current_nwc - prev_nwc

        # Free Cash Flow = NOPAT - CapEx - Δ NWC
        fcf = nopat - capex - nwc_change

        # Store results
        financials['year'].append(year)
        financials['revenue'].append(revenue)
        financials['revenue_growth'].append(growth)
        financials['gross_profit'].append(gross_profit)
        financials['gross_margin'].append(gross_margin)
        financials['opex'].append(opex)
        financials['ebitda'].append(ebitda)
        financials['ebitda_margin'].append(ebitda_margin)
        financials['ebit'].append(ebit)
        financials['tax'].append(tax)
        financials['nopat'].append(nopat)
        financials['capex'].append(capex)
        financials['nwc_change'].append(nwc_change)
        financials['fcf'].append(fcf)

        prev_revenue = revenue
        prev_nwc = current_nwc

    return financials


def calculate_terminal_value(
    final_fcf: float,
    wacc: float,
    terminal_growth: float
) -> float:
    """
    Calculate terminal value using perpetuity growth method.

    Args:
        final_fcf: FCF in the final explicit year
        wacc: Weighted average cost of capital
        terminal_growth: Perpetuity growth rate

    Returns:
        Terminal value in $M
    """
    if wacc <= terminal_growth:
        raise ValueError("WACC must be greater than terminal growth rate")
    return (final_fcf * (1.0 + terminal_growth)) / (wacc - terminal_growth)


def calculate_enterprise_value(
    fcfs: List[float],
    terminal_value: float,
    wacc: float
) -> float:
    """
    Calculate enterprise value by discounting FCFs and terminal value.

    Args:
        fcfs: List of free cash flows (years 1-5)
        terminal_value: Terminal value at end of year 5
        wacc: Discount rate

    Returns:
        Enterprise value in $M
    """
    pv_fcf = 0.0
    for year, fcf in enumerate(fcfs, start=1):
        discount_factor = 1.0 / ((1.0 + wacc) ** year)
        pv_fcf += fcf * discount_factor

    # Discount terminal value (at end of year 5)
    discount_factor_tv = 1.0 / ((1.0 + wacc) ** len(fcfs))
    pv_terminal_value = terminal_value * discount_factor_tv

    enterprise_value = pv_fcf + pv_terminal_value
    return enterprise_value, pv_fcf, pv_terminal_value


def equity_value_bridge(
    enterprise_value: float,
    net_debt: float,
    shares_outstanding: float
) -> Tuple[float, float]:
    """
    Convert enterprise value to equity value and per-share value.

    Args:
        enterprise_value: EV in $M
        net_debt: Net debt (negative = net cash) in $M
        shares_outstanding: Shares outstanding in millions

    Returns:
        Tuple: (equity_value in $M, value_per_share in $)
    """
    equity_value = enterprise_value - net_debt  # Subtracting net debt (or adding net cash if negative)
    value_per_share = (equity_value / shares_outstanding) if shares_outstanding > 0 else 0
    return equity_value, value_per_share


def sensitivity_table_wacc_tgr(
    financials: Dict,
    base_wacc: float,
    base_tgr: float,
    wacc_range: List[float],
    tgr_range: List[float],
    tax_rate: float,
    shares_outstanding: float,
    net_debt: float,
) -> Dict:
    """
    Generate sensitivity table: WACC vs Terminal Growth Rate.

    Args:
        financials: Output from project_financials()
        base_wacc: Base WACC for reference
        base_tgr: Base terminal growth rate
        wacc_range: List of WACC values to test (e.g., [0.10, 0.11, ..., 0.16])
        tgr_range: List of TGR values to test (e.g., [0.015, 0.02, ..., 0.035])
        tax_rate: Tax rate for recalc
        shares_outstanding: For per-share conversion
        net_debt: For equity bridge

    Returns:
        Dict with sensitivity table data
    """
    table = {}
    final_fcf = financials['fcf'][-1]

    for wacc in wacc_range:
        table[f"{wacc:.3f}"] = {}
        for tgr in tgr_range:
            if wacc > tgr:  # Valid only if WACC > TGR
                tv = calculate_terminal_value(final_fcf, wacc, tgr)
                ev, _, _ = calculate_enterprise_value(financials['fcf'], tv, wacc)
                _, price_per_share = equity_value_bridge(ev, net_debt, shares_outstanding)
                table[f"{wacc:.3f}"][f"{tgr:.3f}"] = round(price_per_share, 2)
            else:
                table[f"{wacc:.3f}"][f"{tgr:.3f}"] = None  # Invalid

    return table


def run_dcf_scenario(
    scenario_name: str,
    starting_revenue: float,
    growth_rates: List[float],
    gross_margins: List[float],
    opex_pcts: List[float],
    capex_pct: float,
    nwc_pct_increase: float,
    terminal_growth: float,
    wacc: float,
    tax_rate: float,
    shares_outstanding: float,
    net_debt: float,
) -> Dict:
    """
    Run complete DCF for a single scenario.

    Returns:
        Dict with all results for this scenario
    """
    # Project financials
    # Convert single capex_pct to list (same for all years)
    capex_pcts = [capex_pct] * 5
    financials = project_financials(
        starting_revenue, growth_rates, gross_margins, opex_pcts,
        tax_rate, capex_pcts, nwc_pct_increase
    )

    # Terminal value
    final_fcf = financials['fcf'][-1]
    terminal_value = calculate_terminal_value(final_fcf, wacc, terminal_growth)

    # Enterprise value
    ev, pv_fcf, pv_tv = calculate_enterprise_value(financials['fcf'], terminal_value, wacc)

    # Equity value and per-share
    equity_value, price_per_share = equity_value_bridge(ev, net_debt, shares_outstanding)

    # Terminal value as % of EV
    tv_pct_ev = (pv_tv / ev * 100) if ev > 0 else 0

    return {
        'scenario': scenario_name,
        'financials': financials,
        'wacc': wacc,
        'terminal_growth': terminal_growth,
        'terminal_value': terminal_value,
        'enterprise_value': ev,
        'pv_fcf': pv_fcf,
        'pv_terminal_value': pv_tv,
        'tv_pct_of_ev': tv_pct_ev,
        'equity_value': equity_value,
        'price_per_share': price_per_share,
    }


