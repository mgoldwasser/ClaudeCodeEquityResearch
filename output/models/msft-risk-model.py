def calc_returns(prices: List[float]) -> List[float]:
    """
    Calculate logarithmic returns from price series.

    Args:
        prices: List of prices (chronological order)

    Returns:
        List of log returns (length = len(prices) - 1)
    """
    returns = []
    for i in range(1, len(prices)):
        if prices[i - 1] > 0 and prices[i] > 0:
            returns.append(math.log(prices[i] / prices[i - 1]))
    return returns


def calc_historical_volatility(returns: List[float]) -> Dict:
    """
    Calculate annualized volatility from monthly returns.

    Args:
        returns: List of monthly log returns

    Returns:
        Dict with monthly_vol, annualized_vol, annualized_pct
    """
    n = len(returns)
    if n < 2:
        return {'monthly_vol': 0, 'annualized_vol': 0, 'annualized_pct': 0}

    mean = sum(returns) / n
    variance = sum((r - mean) ** 2 for r in returns) / (n - 1)
    monthly_vol = math.sqrt(variance)
    annualized_vol = monthly_vol * math.sqrt(12)

    return {
        'monthly_vol': monthly_vol,
        'annualized_vol': annualized_vol,
        'annualized_pct': annualized_vol * 100,
        'mean_monthly_return': mean,
        'mean_annualized_return': mean * 12
    }


def calc_var_parametric(returns: List[float], confidence: float,
                        current_value: float = 1.0) -> Dict:
    """
    Calculate parametric VaR assuming normal distribution.

    Args:
        returns: List of periodic returns
        confidence: Confidence level (e.g., 0.95)
        current_value: Portfolio/position value

    Returns:
        Dict with var_pct, var_dollar, method
    """
    n = len(returns)
    mean = sum(returns) / n
    std = math.sqrt(sum((r - mean) ** 2 for r in returns) / (n - 1))

    # Z-scores for normal distribution
    z_scores = {0.95: 1.645, 0.99: 2.326}
    z = z_scores.get(confidence, 1.645)

    var_pct = mean - z * std
    var_dollar = current_value * abs(var_pct)

    return {
        'var_pct': var_pct * 100,
        'var_dollar': var_dollar,
        'method': 'parametric',
        'confidence': confidence * 100,
        'z_score': z
    }


def calc_var_historical(returns: List[float], confidence: float,
                        current_value: float = 1.0) -> Dict:
    """
    Calculate historical VaR from sorted return distribution.

    Args:
        returns: List of periodic returns
        confidence: Confidence level
        current_value: Position value

    Returns:
        Dict with var_pct, var_dollar, method
    """
    sorted_returns = sorted(returns)
    index = int((1 - confidence) * len(sorted_returns))
    var_return = sorted_returns[index]
    var_dollar = current_value * abs(var_return)

    # CVaR: average of returns worse than VaR
    worse_returns = sorted_returns[:index + 1]
    cvar_return = sum(worse_returns) / len(worse_returns) if worse_returns else var_return
    cvar_dollar = current_value * abs(cvar_return)

    return {
        'var_pct': var_return * 100,
        'var_dollar': var_dollar,
        'cvar_pct': cvar_return * 100,
        'cvar_dollar': cvar_dollar,
        'method': 'historical',
        'confidence': confidence * 100,
        'worst_return_pct': sorted_returns[0] * 100
    }


def monte_carlo_simulation(current_price: float, annual_drift: float,
                           annual_vol: float, months: int,
                           n_simulations: int, seed: int) -> Dict:
    """
    Run Monte Carlo price simulation.

    Args:
        current_price: Starting price
        annual_drift: Expected annual return (decimal)
        annual_vol: Annualized volatility (decimal)
        months: Simulation horizon in months
        n_simulations: Number of simulation paths
        seed: Random seed for reproducibility

    Returns:
        Dict with final_prices, statistics, percentiles
    """
    random.seed(seed)

    monthly_drift = annual_drift / 12
    monthly_vol = annual_vol / math.sqrt(12)

    final_prices = []
    max_drawdowns = []
    all_paths = []

    for _ in range(n_simulations):
        price = current_price
        peak = price
        max_dd = 0
        path = [price]

        for _ in range(months):
            # Geometric Brownian Motion
            z = random.gauss(0, 1)
            monthly_return = monthly_drift - 0.5 * monthly_vol ** 2 + monthly_vol * z
            price = price * math.exp(monthly_return)
            path.append(price)

            if price > peak:
                peak = price
            dd = (peak - price) / peak
            if dd > max_dd:
                max_dd = dd

        final_prices.append(price)
        max_drawdowns.append(max_dd)
        if len(all_paths) < 10:  # Store first 10 paths for display
            all_paths.append(path)

    final_prices.sort()
    max_drawdowns.sort()
    n = len(final_prices)

    # Statistics
    mean_price = sum(final_prices) / n
    returns = [(p / current_price - 1) for p in final_prices]
    mean_return = sum(returns) / n

    # Percentiles
    def pct(data, p):
        k = int(p / 100 * (len(data) - 1))
        return data[k]

    return {
        'n_simulations': n_simulations,
        'horizon_months': months,
        'annual_drift': annual_drift,
        'annual_vol': annual_vol,
        'mean_final_price': mean_price,
        'mean_return_pct': mean_return * 100,
        'median_final_price': pct(final_prices, 50),
        'p5_price': pct(final_prices, 5),
        'p10_price': pct(final_prices, 10),
        'p25_price': pct(final_prices, 25),
        'p75_price': pct(final_prices, 75),
        'p90_price': pct(final_prices, 90),
        'p95_price': pct(final_prices, 95),
        'min_price': final_prices[0],
        'max_price': final_prices[-1],
        'prob_above_current': sum(1 for p in final_prices if p > current_price) / n * 100,
        'prob_above_500': sum(1 for p in final_prices if p > 500) / n * 100,
        'prob_below_300': sum(1 for p in final_prices if p < 300) / n * 100,
        'mean_max_drawdown_pct': sum(max_drawdowns) / n * 100,
        'median_max_drawdown_pct': pct(max_drawdowns, 50) * 100,
        'p95_max_drawdown_pct': pct(max_drawdowns, 95) * 100,
        'sample_paths': all_paths
    }


def drawdown_analysis(prices: List[float]) -> Dict:
    """
    Analyze historical drawdowns from price series.

    Args:
        prices: List of prices (chronological)

    Returns:
        Dict with max_drawdown, drawdown_periods, current_drawdown
    """
    peak = prices[0]
    max_dd = 0
    max_dd_peak = prices[0]
    max_dd_trough = prices[0]
    current_dd = 0
    drawdowns = []
    current_peak = prices[0]
    in_drawdown = False
    dd_start = 0

    for i, price in enumerate(prices):
        if price > current_peak:
            if in_drawdown and current_dd > 0.05:
                drawdowns.append({
                    'start_idx': dd_start,
                    'trough_idx': trough_idx,
                    'end_idx': i,
                    'peak_price': current_peak,
                    'trough_price': trough_price,
                    'drawdown_pct': current_dd * 100,
                    'recovery_months': i - trough_idx
                })
            current_peak = price
            in_drawdown = False
            current_dd = 0
        else:
            dd = (current_peak - price) / current_peak
            if dd > 0 and not in_drawdown:
                in_drawdown = True
                dd_start = i - 1
                trough_price = price
                trough_idx = i
            if dd > current_dd:
                current_dd = dd
                trough_price = price
                trough_idx = i
            if dd > max_dd:
                max_dd = dd
                max_dd_peak = current_peak
                max_dd_trough = price

    # Current drawdown from all-time high in the series
    series_peak = max(prices)
    current_from_peak = (series_peak - prices[-1]) / series_peak

    return {
        'max_drawdown_pct': max_dd * 100,
        'max_dd_peak': max_dd_peak,
        'max_dd_trough': max_dd_trough,
        'current_drawdown_pct': current_from_peak * 100,
        'series_peak': series_peak,
        'current_price': prices[-1],
        'significant_drawdowns': drawdowns,
        'n_drawdowns_gt_10pct': sum(1 for d in drawdowns if d['drawdown_pct'] > 10),
        'n_drawdowns_gt_20pct': sum(1 for d in drawdowns if d['drawdown_pct'] > 20)
    }


def correlation_analysis(returns1: List[float], returns2: List[float],
                         name1: str, name2: str) -> Dict:
    """
    Calculate correlation between two return series.

    Args:
        returns1, returns2: Return series (must be same length)
        name1, name2: Names for the series

    Returns:
        Dict with correlation, r_squared
    """
    n = min(len(returns1), len(returns2))
    r1 = returns1[:n]
    r2 = returns2[:n]

    mean1 = sum(r1) / n
    mean2 = sum(r2) / n

    cov = sum((r1[i] - mean1) * (r2[i] - mean2) for i in range(n)) / (n - 1)
    var1 = sum((r - mean1) ** 2 for r in r1) / (n - 1)
    var2 = sum((r - mean2) ** 2 for r in r2) / (n - 1)

    std1 = math.sqrt(var1)
    std2 = math.sqrt(var2)

    if std1 > 0 and std2 > 0:
        correlation = cov / (std1 * std2)
    else:
        correlation = 0

    return {
        'pair': f"{name1} vs {name2}",
        'correlation': correlation,
        'r_squared': correlation ** 2,
        'n_observations': n
    }


def risk_adjusted_returns(expected_return: float, volatility: float,
                          risk_free: float) -> Dict:
    """
    Calculate risk-adjusted return metrics.

    Args:
        expected_return: Expected annual return (decimal)
        volatility: Annualized volatility (decimal)
        risk_free: Risk-free rate (decimal)

    Returns:
        Dict with Sharpe, Sortino (approx), Kelly fraction
    """
    sharpe = (expected_return - risk_free) / volatility if volatility > 0 else 0

    # Approximate Sortino (using vol as downside deviation proxy with 0.7 factor)
    downside_vol = volatility * 0.7  # Approximation
    sortino = (expected_return - risk_free) / downside_vol if downside_vol > 0 else 0

    # Kelly fraction
    if volatility > 0:
        kelly = (expected_return - risk_free) / (volatility ** 2)
    else:
        kelly = 0

    return {
        'sharpe_ratio': sharpe,
        'sortino_ratio': sortino,
        'kelly_fraction': kelly,
        'half_kelly': kelly / 2,
        'quarter_kelly': kelly / 4
    }


def stress_test(current_price: float, scenarios: Dict) -> Dict:
    """
    Run stress test scenarios.

    Args:
        current_price: Current stock price
        scenarios: Dict of scenario definitions

    Returns:
        Dict with stressed prices and expected loss
    """
    results = {}
    expected_loss = 0

    for name, scenario in scenarios.items():
        shocked_price = current_price * (1 + scenario['price_shock_pct'])
        dollar_impact = shocked_price - current_price
        prob = scenario['probability']

        results[name] = {
            'description': scenario['description'],
            'price_shock_pct': scenario['price_shock_pct'] * 100,
            'shocked_price': shocked_price,
            'dollar_impact': dollar_impact,
            'probability': prob * 100
        }

        expected_loss += prob * dollar_impact

    results['expected_value_impact'] = expected_loss
    results['probability_weighted_price'] = current_price + expected_loss

    return results


