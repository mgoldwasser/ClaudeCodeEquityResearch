def calc_multiples(company: Dict) -> Dict:
    """
    Calculate valuation multiples for a company.

    Args:
        company: Dict with financial data fields.

    Returns:
        Dict with calculated multiples. None values indicate incalculable metrics.
    """
    ev = company["enterprise_value_M"]
    rev = company["revenue_M"]
    ebitda = company["ebitda_M"]
    ni = company["net_income_M"]
    fcf = company["fcf_M"]
    mcap = company["market_cap_M"]
    eps_fwd = company.get("eps_forward", 0)
    eps_growth = company.get("eps_growth_pct", 0)
    price = company.get("price", 0)

    ev_rev = ev / rev if rev > 0 else None
    ev_ebitda = ev / ebitda if ebitda and ebitda > 0 else None
    pe_trailing = mcap / ni if ni and ni > 0 else None
    pe_forward = price / eps_fwd if eps_fwd and eps_fwd > 0 else None
    peg = pe_forward / eps_growth if pe_forward and eps_growth and eps_growth > 0 else None
    ev_fcf = ev / fcf if fcf and fcf > 0 else None

    return {
        "ticker": company["ticker"],
        "ev_revenue": round(ev_rev, 2) if ev_rev else None,
        "ev_ebitda": round(ev_ebitda, 1) if ev_ebitda else None,
        "pe_trailing": round(pe_trailing, 1) if pe_trailing else None,
        "pe_forward": round(pe_forward, 1) if pe_forward else None,
        "peg": round(peg, 2) if peg else None,
        "ev_fcf": round(ev_fcf, 1) if ev_fcf else None,
        "gross_margin_pct": company.get("gross_margin_pct"),
        "revenue_growth_pct": company.get("revenue_growth_pct"),
    }


def calc_statistics(values: List[float]) -> Dict:
    """
    Calculate summary statistics for a list of values (excluding None).

    Args:
        values: List of float values (may contain None).

    Returns:
        Dict with mean, median, std_dev, min, max, q25, q75, count.
    """
    clean = sorted([v for v in values if v is not None])
    n = len(clean)
    if n == 0:
        return {"mean": None, "median": None, "std_dev": None, "min": None,
                "max": None, "q25": None, "q75": None, "count": 0}

    mean = sum(clean) / n
    if n % 2 == 1:
        median = clean[n // 2]
    else:
        median = (clean[n // 2 - 1] + clean[n // 2]) / 2

    variance = sum((x - mean) ** 2 for x in clean) / max(n - 1, 1)
    std_dev = math.sqrt(variance)

    def percentile(data, pct):
        k = (len(data) - 1) * pct
        f = math.floor(k)
        c = math.ceil(k)
        if f == c:
            return data[int(k)]
        return data[f] * (c - k) + data[c] * (k - f)

    q25 = percentile(clean, 0.25)
    q75 = percentile(clean, 0.75)

    return {
        "mean": round(mean, 2),
        "median": round(median, 2),
        "std_dev": round(std_dev, 2),
        "min": round(min(clean), 2),
        "max": round(max(clean), 2),
        "q25": round(q25, 2),
        "q75": round(q75, 2),
        "count": n,
    }


def calc_z_scores(subject_multiples: Dict, comp_multiples_list: List[Dict]) -> Dict:
    """
    Calculate z-scores for the subject company relative to comp set.

    Args:
        subject_multiples: Multiples dict for the subject company.
        comp_multiples_list: List of multiples dicts for comparables.

    Returns:
        Dict of metric -> z-score. Positive = subject trades at premium.
    """
    metrics = ["ev_revenue", "ev_ebitda", "pe_forward", "peg", "ev_fcf"]
    z_scores = {}
    for m in metrics:
        subj_val = subject_multiples.get(m)
        comp_vals = [c.get(m) for c in comp_multiples_list if c.get(m) is not None]
        if subj_val is None or len(comp_vals) < 2:
            z_scores[m] = None
            continue
        stats = calc_statistics(comp_vals)
        if stats["std_dev"] and stats["std_dev"] > 0:
            z = (subj_val - stats["mean"]) / stats["std_dev"]
            z_scores[m] = round(z, 2)
        else:
            z_scores[m] = None
    return z_scores


def flag_outliers(comp_multiples: List[Dict], threshold: float = 2.0) -> List[Dict]:
    """
    Flag companies with multiples > threshold standard deviations from mean.

    Args:
        comp_multiples: List of company multiples dicts.
        threshold: Z-score threshold for outlier flag.

    Returns:
        List of dicts with 'ticker', 'metric', 'value', 'z_score'.
    """
    metrics = ["ev_revenue", "ev_ebitda", "pe_forward", "peg", "ev_fcf"]
    outliers = []
    for m in metrics:
        vals = [(c["ticker"], c.get(m)) for c in comp_multiples if c.get(m) is not None]
        if len(vals) < 3:
            continue
        numbers = [v[1] for v in vals]
        stats = calc_statistics(numbers)
        if not stats["std_dev"] or stats["std_dev"] == 0:
            continue
        for ticker, val in vals:
            z = (val - stats["mean"]) / stats["std_dev"]
            if abs(z) > threshold:
                outliers.append({
                    "ticker": ticker,
                    "metric": m,
                    "value": val,
                    "z_score": round(z, 2),
                })
    return outliers


def implied_valuation(subject: Dict, comp_stats: Dict, metric: str,
                      subject_financial: float) -> Dict:
    """
    Calculate implied valuation range from comp multiples applied to subject financials.

    Args:
        subject: Subject company data dict.
        comp_stats: Statistics dict for the given metric.
        metric: Multiple name (e.g., 'ev_ebitda').
        subject_financial: The financial figure to multiply (e.g., EBITDA in $M).

    Returns:
        Dict with implied EV at q25, median, q75, mean and derived share prices.
    """
    shares = subject["shares_M"]
    net_cash_M = subject.get("market_cap_M", 0) - subject.get("enterprise_value_M", 0)
    # Warrant dilution at 50% base case
    diluted_shares = shares + 320 * 0.5

    result = {}
    for label, stat_val in [("q25", comp_stats["q25"]), ("median", comp_stats["median"]),
                            ("q75", comp_stats["q75"]), ("mean", comp_stats["mean"])]:
        if stat_val is None:
            result[label] = {"ev_M": None, "price": None}
            continue
        implied_ev = subject_financial * stat_val
        implied_equity = implied_ev + net_cash_M
        implied_price = implied_equity / diluted_shares if diluted_shares > 0 else 0
        result[label] = {
            "ev_M": round(implied_ev, 0),
            "price": round(implied_price, 2),
        }
    return result


def composite_rank(companies: List[Dict], multiples: List[Dict]) -> List[Dict]:
    """
    Rank companies by composite score (value + quality + growth).

    Args:
        companies: List of company data dicts.
        multiples: Corresponding multiples dicts.

    Returns:
        Sorted list of dicts with 'ticker', 'composite_score', 'value_rank', etc.
    """
    scores = []
    n = len(companies)
    for i, (co, mult) in enumerate(zip(companies, multiples)):
        # Value: lower multiples = better (invert rank)
        val_score = 0
        count = 0
        for m in ["ev_ebitda", "pe_forward", "ev_fcf"]:
            if mult.get(m) is not None:
                val_score += mult[m]
                count += 1
        avg_val = val_score / count if count > 0 else 999

        # Quality: higher margins = better
        gm = co.get("gross_margin_pct", 0) or 0

        # Growth: higher = better
        rev_g = co.get("revenue_growth_pct", 0) or 0
        peg = mult.get("peg") if mult.get("peg") and mult["peg"] > 0 else 5

        scores.append({
            "ticker": co["ticker"],
            "avg_multiple": round(avg_val, 1),
            "gross_margin": gm,
            "revenue_growth": rev_g,
            "peg": round(peg, 2),
        })

    # Rank each dimension
    sorted_by_val = sorted(range(n), key=lambda i: scores[i]["avg_multiple"])
    sorted_by_gm = sorted(range(n), key=lambda i: -scores[i]["gross_margin"])
    sorted_by_growth = sorted(range(n), key=lambda i: -scores[i]["revenue_growth"])
    sorted_by_peg = sorted(range(n), key=lambda i: scores[i]["peg"])

    for idx in range(n):
        val_rank = sorted_by_val.index(idx) + 1
        qual_rank = sorted_by_gm.index(idx) + 1
        growth_rank = sorted_by_growth.index(idx) + 1
        peg_rank = sorted_by_peg.index(idx) + 1
        composite = (val_rank + qual_rank + growth_rank + peg_rank) / 4
        scores[idx]["value_rank"] = val_rank
        scores[idx]["quality_rank"] = qual_rank
        scores[idx]["growth_rank"] = growth_rank
        scores[idx]["peg_rank"] = peg_rank
        scores[idx]["composite_score"] = round(composite, 1)

    return sorted(scores, key=lambda x: x["composite_score"])


