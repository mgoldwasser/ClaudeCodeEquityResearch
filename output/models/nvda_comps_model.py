def calculate_multiples(company: Dict) -> Dict:
    """
    Calculate all key valuation multiples for a company.

    Args:
        company: Dict with revenue, ebitda, net_income, market_cap, ev

    Returns:
        Dict of calculated multiples
    """
    # Support both 'revenue_ltm_m' and 'revenue_fy2026_m' keys
    revenue_key = 'revenue_ltm_m' if 'revenue_ltm_m' in company else 'revenue_fy2026_m'
    ebitda_key = 'ebitda_ltm_m' if 'ebitda_ltm_m' in company else 'ebitda_fy2026_m'
    ni_key = 'net_income_ltm_m' if 'net_income_ltm_m' in company else 'net_income_fy2026_m'

    rev_val = company.get(revenue_key, 0)
    ebitda_val = company.get(ebitda_key, 0)
    ni_val = company.get(ni_key, 0)

    multiples = {
        'ev_to_revenue': (company['ev_market_cap_m'] / rev_val if rev_val > 0 else None),
        'ev_to_ebitda': (company['ev_market_cap_m'] / ebitda_val if ebitda_val > 0 else None),
        'pe_ratio': (company['market_cap_m'] / ni_val if ni_val > 0 else None),
        'price_to_revenue': (company['market_cap_m'] / rev_val if rev_val > 0 else None),
    }
    return multiples


def calculate_statistics(values: List[float]) -> Dict:
    """
    Calculate summary statistics for a list of multiples.

    Args:
        values: List of numeric values

    Returns:
        Dict with mean, median, stdev, min, max, q25, q75
    """
    valid_values = [v for v in values if v is not None and v > 0]

    if not valid_values:
        return {
            'count': 0,
            'mean': None,
            'median': None,
            'stdev': None,
            'min': None,
            'max': None,
            'q25': None,
            'q75': None,
        }

    valid_values_sorted = sorted(valid_values)
    n = len(valid_values)

    mean_val = statistics.mean(valid_values)
    median_val = statistics.median(valid_values)
    stdev_val = statistics.stdev(valid_values) if n > 1 else 0
    min_val = min(valid_values)
    max_val = max(valid_values)

    # Quartiles
    q25_idx = int(n * 0.25)
    q75_idx = int(n * 0.75)
    q25_val = valid_values_sorted[q25_idx] if q25_idx < n else valid_values_sorted[-1]
    q75_val = valid_values_sorted[q75_idx] if q75_idx < n else valid_values_sorted[-1]

    return {
        'count': n,
        'mean': round(mean_val, 2),
        'median': round(median_val, 2),
        'stdev': round(stdev_val, 2),
        'min': round(min_val, 2),
        'max': round(max_val, 2),
        'q25': round(q25_val, 2),
        'q75': round(q75_val, 2),
    }


def detect_outliers(values: List[float], threshold_std: float = 2.0) -> List[Tuple[int, float]]:
    """
    Detect outlier multiples using standard deviation threshold.

    Args:
        values: List of numeric values
        threshold_std: Number of standard deviations (default 2.0)

    Returns:
        List of (index, value) tuples for outliers
    """
    valid_values = [v for v in values if v is not None and v > 0]
    if not valid_values or len(valid_values) < 2:
        return []

    mean_val = statistics.mean(valid_values)
    stdev_val = statistics.stdev(valid_values)

    outliers = []
    for i, v in enumerate(values):
        if v is not None and v > 0:
            z_score = abs((v - mean_val) / stdev_val) if stdev_val > 0 else 0
            if z_score > threshold_std:
                outliers.append((i, v, z_score))

    return outliers


def implied_valuation(
    subject_metric_value: float,
    multiple: float,
) -> Optional[float]:
    """
    Calculate implied valuation from a single multiple.

    Args:
        subject_metric_value: Numerator (e.g., subject's revenue)
        multiple: Multiple to apply (e.g., peer avg EV/Revenue)

    Returns:
        Implied value
    """
    if multiple is None or multiple <= 0:
        return None
    return subject_metric_value * multiple


