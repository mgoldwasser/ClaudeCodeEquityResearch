    ev_rev_values = [c['multiples']['ev_to_revenue'] for c in comp_multiples_all]
    ev_ebitda_values = [c['multiples']['ev_to_ebitda'] for c in comp_multiples_all]
    pe_values = [c['multiples']['pe_ratio'] for c in comp_multiples_all]

    results['statistics']['EV/Revenue'] = calculate_statistics(ev_rev_values)
    results['statistics']['EV/EBITDA'] = calculate_statistics(ev_ebitda_values)
    results['statistics']['P/E'] = calculate_statistics(pe_values)

