def calc_multiples(data: Dict) -> Dict:
    """
    Calculate valuation multiples for a company.

    Args:
        data: Company financial data dict

    Returns:
        Dict with EV/EBITDA, P/E, EV/Revenue, PEG multiples
    """
    ev = data['market_cap'] + data['net_debt']

    ev_ebitda = ev / data['ebitda_ltm'] if data['ebitda_ltm'] > 0 else None
    pe = data['price'] / data['eps_ntm'] if data['eps_ntm'] > 0 else None
    ev_revenue = ev / data['revenue_ltm'] if data['revenue_ltm'] > 0 else None
    peg = pe / data['eps_growth_pct'] if pe and data['eps_growth_pct'] > 0 else None

    return {
        'ticker': [k for k, v in COMP_DATA.items() if v == data][0] if data in COMP_DATA.values() else 'UNK',
        'name': data['name'],
        'enterprise_value': ev,
        'ev_ebitda': ev_ebitda,
        'pe': pe,
        'ev_revenue': ev_revenue,
        'peg': peg,
        'revenue_growth': data['revenue_growth_pct'],
        'eps_growth': data['eps_growth_pct'],
        'is_subject': data.get('is_subject', False)
    }


def calc_statistics(values: List[float]) -> Dict:
    """
    Calculate summary statistics for a list of values.

    Args:
        values: List of numerical values (None values excluded)

    Returns:
        Dict with mean, median, std_dev, min, max, p25, p75
    """
    clean = sorted([v for v in values if v is not None])
    if not clean:
        return {'mean': None, 'median': None, 'std_dev': None,
                'min': None, 'max': None, 'p25': None, 'p75': None}

    n = len(clean)
    mean = sum(clean) / n
    median = clean[n // 2] if n % 2 == 1 else (clean[n // 2 - 1] + clean[n // 2]) / 2
    variance = sum((x - mean) ** 2 for x in clean) / n if n > 1 else 0
    std_dev = math.sqrt(variance)

    # Percentiles (simple interpolation)
    def percentile(data, pct):
        k = (len(data) - 1) * pct / 100
        f = int(k)
        c = f + 1
        if c >= len(data):
            return data[f]
        return data[f] + (k - f) * (data[c] - data[f])

    p25 = percentile(clean, 25)
    p75 = percentile(clean, 75)

    return {
        'mean': mean,
        'median': median,
        'std_dev': std_dev,
        'min': clean[0],
        'max': clean[-1],
        'p25': p25,
        'p75': p75,
        'count': n
    }


def calc_z_scores(subject_multiples: Dict, comp_stats: Dict) -> Dict:
    """
    Calculate z-scores for subject company vs comp set.

    Args:
        subject_multiples: Subject company's multiples
        comp_stats: Statistics from comp set

    Returns:
        Dict with z-scores for each multiple
    """
    z_scores = {}
    for metric in ['ev_ebitda', 'pe', 'ev_revenue', 'peg']:
        subj_val = subject_multiples.get(metric)
        stats = comp_stats.get(metric, {})
        mean = stats.get('mean')
        std = stats.get('std_dev')

        if subj_val is not None and mean is not None and std and std > 0:
            z = (subj_val - mean) / std
            z_scores[metric] = round(z, 2)
        else:
            z_scores[metric] = None

    return z_scores


def implied_valuation(subject_data: Dict, comp_stats: Dict) -> Dict:
    """
    Calculate implied valuation at various comp multiples.

    Args:
        subject_data: Subject company financial data
        comp_stats: Statistics from comp set

    Returns:
        Dict with implied share prices at p25, median, mean, p75 multiples
    """
    results = {}

    for metric_name, fin_metric, bridge_method in [
        ('ev_ebitda', 'ebitda_ltm', 'ev_to_equity'),
        ('ev_revenue', 'revenue_ltm', 'ev_to_equity'),
        ('pe', 'eps_ntm', 'direct'),
    ]:
        stats = comp_stats.get(metric_name, {})
        if not stats.get('median'):
            continue

        implied = {}
        for stat_name in ['p25', 'median', 'mean', 'p75']:
            mult = stats.get(stat_name)
            if mult is None:
                continue

            if bridge_method == 'ev_to_equity':
                implied_ev = mult * subject_data[fin_metric]
                implied_equity = implied_ev - subject_data['net_debt']
                implied_price = implied_equity / subject_data['shares_out']
            else:
                implied_price = mult * subject_data[fin_metric]

            implied[stat_name] = round(implied_price, 2)

        results[metric_name] = implied

    return results


def flag_outliers(multiples_list: List[Dict], threshold: float = 2.0) -> List[str]:
    """
    Flag companies with multiples >threshold standard deviations from mean.

    Args:
        multiples_list: List of company multiples dicts
        threshold: Number of standard deviations for outlier flag

    Returns:
        List of outlier descriptions
    """
    outliers = []
    comp_only = [m for m in multiples_list if not m.get('is_subject')]

    for metric in ['ev_ebitda', 'pe', 'ev_revenue', 'peg']:
        values = [m[metric] for m in comp_only if m[metric] is not None]
        if len(values) < 3:
            continue

        mean = sum(values) / len(values)
        std = math.sqrt(sum((x - mean) ** 2 for x in values) / len(values))
        if std == 0:
            continue

        for m in multiples_list:
            val = m.get(metric)
            if val is not None:
                z = abs((val - mean) / std)
                if z > threshold:
                    outliers.append(
                        f"{m['name']} {metric}={val:.1f} (z={z:+.1f}, "
                        f"mean={mean:.1f}, std={std:.1f})"
                    )

    return outliers


def composite_rank(multiples_list: List[Dict]) -> List[Dict]:
    """
    Rank companies by composite score (value + growth).

    Args:
        multiples_list: List of company multiples dicts

    Returns:
        Sorted list with composite scores
    """
    for m in multiples_list:
        # Lower PEG = better value-adjusted growth
        # Lower EV/Revenue = cheaper
        scores = []
        if m['peg'] is not None:
            scores.append(1 / m['peg'] if m['peg'] > 0 else 0)
        if m['revenue_growth'] is not None:
            scores.append(m['revenue_growth'] / 100)
        if m['ev_revenue'] is not None:
            scores.append(1 / m['ev_revenue'] if m['ev_revenue'] > 0 else 0)

        m['composite_score'] = sum(scores) / len(scores) if scores else 0

    return sorted(multiples_list, key=lambda x: x['composite_score'], reverse=True)


