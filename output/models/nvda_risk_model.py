def monte_carlo_valuation(
    base_fair_value: float,
    bull_fair_value: float,
    bear_fair_value: float,
    volatility: float,
    num_simulations: int = 10_000,
    seed: int = 42,
) -> Dict:
    """
    Run Monte Carlo simulation on valuation (normally distributed returns).

    Args:
        base_fair_value: Base case DCF price target
        bull_fair_value: Bull case price target
        bear_fair_value: Bear case price target
        volatility: Annual volatility
        num_simulations: Number of simulations
        seed: Random seed

    Returns:
        Dict with simulation results (mean, median, percentiles, histogram)
    """
    if HAS_NUMPY:
        np.random.seed(seed)
        # Simulate returns as normally distributed
        returns = np.random.normal(0, volatility, num_simulations)
        # Implied prices (if holding for 1 year from current base case)
        simulated_prices = base_fair_value * np.exp(returns)
    else:
        rng = RandomGenerator(seed=seed)
        simulated_prices = []
        for _ in range(num_simulations):
            r = rng.normal(0, volatility)
            price = base_fair_value * math.exp(r)
            simulated_prices.append(price)
        simulated_prices = list(simulated_prices)

    if HAS_NUMPY:
        prices_sorted = np.sort(simulated_prices)
        mean_price = np.mean(simulated_prices)
        median_price = np.median(simulated_prices)
        p5 = np.percentile(simulated_prices, 5)
        p25 = np.percentile(simulated_prices, 25)
        p75 = np.percentile(simulated_prices, 75)
        p95 = np.percentile(simulated_prices, 95)
        std_dev = np.std(simulated_prices)
    else:
        prices_sorted = sorted(simulated_prices)
        mean_price = sum(prices_sorted) / len(prices_sorted)
        median_price = prices_sorted[len(prices_sorted) // 2]
        p5 = prices_sorted[int(0.05 * len(prices_sorted))]
        p25 = prices_sorted[int(0.25 * len(prices_sorted))]
        p75 = prices_sorted[int(0.75 * len(prices_sorted))]
        p95 = prices_sorted[int(0.95 * len(prices_sorted))]
        variance = sum((p - mean_price) ** 2 for p in prices_sorted) / len(prices_sorted)
        std_dev = math.sqrt(variance)

    # Probability of reaching bull/bear case
    bull_prob = sum(1 for p in simulated_prices if p >= bull_fair_value) / len(simulated_prices)
    bear_prob = sum(1 for p in simulated_prices if p <= bear_fair_value) / len(simulated_prices)

    return {
        'simulations': num_simulations,
        'mean': round(mean_price, 2),
        'median': round(median_price, 2),
        'stdev': round(std_dev, 2),
        'p5': round(p5, 2),
        'p25': round(p25, 2),
        'p75': round(p75, 2),
        'p95': round(p95, 2),
        'min': round(min(simulated_prices), 2),
        'max': round(max(simulated_prices), 2),
        'prob_bull': round(bull_prob, 4),
        'prob_bear': round(bear_prob, 4),
    }


def calculate_var(
    base_price: float,
    volatility: float,
    confidence_level: float = 0.95,
    num_simulations: int = 10_000,
    seed: int = 42,
) -> float:
    """
    Calculate Value at Risk (VaR) — maximum expected loss at given confidence level.

    Args:
        base_price: Current or base fair value
        volatility: Annual volatility
        confidence_level: Confidence level (e.g., 0.95 for 95%)
        num_simulations: Number of simulations
        seed: Random seed

    Returns:
        VaR as percentage loss (e.g., 0.25 for 25% loss)
    """
    if HAS_NUMPY:
        np.random.seed(seed)
        returns = np.random.normal(0, volatility, num_simulations)
        prices = base_price * np.exp(returns)
        losses = (base_price - prices) / base_price
        var = np.percentile(losses, (1 - confidence_level) * 100)
    else:
        rng = RandomGenerator(seed=seed)
        losses = []
        for _ in range(num_simulations):
            r = rng.normal(0, volatility)
            price = base_price * math.exp(r)
            loss = (base_price - price) / base_price
            losses.append(loss)
        losses_sorted = sorted(losses)
        var_idx = int((1 - confidence_level) * len(losses_sorted))
        var = losses_sorted[var_idx]

    return var


def calculate_cvar(
    base_price: float,
    volatility: float,
    confidence_level: float = 0.95,
    num_simulations: int = 10_000,
    seed: int = 42,
) -> float:
    """
    Calculate Conditional Value at Risk (CVaR) — average loss beyond VaR.

    Args:
        base_price: Current or base fair value
        volatility: Annual volatility
        confidence_level: Confidence level (e.g., 0.95)
        num_simulations: Number of simulations
        seed: Random seed

    Returns:
        CVaR as percentage loss
    """
    if HAS_NUMPY:
        np.random.seed(seed)
        returns = np.random.normal(0, volatility, num_simulations)
        prices = base_price * np.exp(returns)
        losses = (base_price - prices) / base_price
        var_threshold = np.percentile(losses, (1 - confidence_level) * 100)
        cvar = np.mean(losses[losses >= var_threshold])
    else:
        rng = RandomGenerator(seed=seed)
        losses = []
        for _ in range(num_simulations):
            r = rng.normal(0, volatility)
            price = base_price * math.exp(r)
            loss = (base_price - price) / base_price
            losses.append(loss)
        losses_sorted = sorted(losses)
        var_idx = int((1 - confidence_level) * len(losses_sorted))
        var_threshold = losses_sorted[var_idx]
        tail_losses = [l for l in losses if l >= var_threshold]
        cvar = sum(tail_losses) / len(tail_losses) if tail_losses else var_threshold

    return cvar


def kelly_criterion(
    win_prob: float,
    avg_win: float,
    avg_loss: float,
) -> float:
    """
    Calculate optimal position size using Kelly criterion.

    Args:
        win_prob: Probability of winning (e.g., 0.65)
        avg_win: Average gain if winning (e.g., 0.30 for 30%)
        avg_loss: Average loss if losing (e.g., 0.15 for 15%)

    Returns:
        Kelly fraction as decimal (e.g., 0.15 for 15% of portfolio)
    """
    # Kelly = (bp - q) / b, where b = avg_win, p = win_prob, q = 1 - p
    win_prob_q = 1.0 - win_prob
    kelly = (avg_win * win_prob - avg_loss * win_prob_q) / avg_win
    kelly = max(0, min(kelly, 1.0))  # Clamp to [0, 1]
    return kelly


def stress_test_scenario(
    base_fair_value: float,
    risk_factors: Dict[str, float],
    scenario_name: str = "combined",
) -> Dict:
    """
    Run stress test scenario with multiple simultaneous shocks.

    Args:
        base_fair_value: Base DCF fair value
        risk_factors: Dict of risk factor impacts (e.g., {'market_share_loss': 0.05})
        scenario_name: Name of scenario

    Returns:
        Dict with stressed valuation and downside
    """
    # Aggregate risk impacts
    total_impact = 1.0
    for risk, impact in risk_factors.items():
        # Impact is multiplicative on valuation (e.g., -5% market share → 5% EV loss)
        total_impact *= (1.0 - impact)

    stressed_value = base_fair_value * total_impact
    downside_pct = (base_fair_value - stressed_value) / base_fair_value

    return {
        'scenario': scenario_name,
        'base_value': round(base_fair_value, 2),
        'stressed_value': round(stressed_value, 2),
        'downside_pct': round(downside_pct, 4),
        'factors_applied': risk_factors,
    }


