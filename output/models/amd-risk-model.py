def calc_var_cvar(returns: List[float], confidence: float = 0.95) -> Dict:
    """
    Calculate Value-at-Risk and Conditional VaR (Expected Shortfall).

    Args:
        returns: List of simulated returns (decimals).
        confidence: Confidence level (e.g., 0.95 for 95%).

    Returns:
        Dict with 'var', 'cvar', 'confidence'.
    """
    sorted_returns = sorted(returns)
    n = len(sorted_returns)
    idx = int(n * (1 - confidence))
    idx = max(idx, 1)

    var = -sorted_returns[idx]  # Positive number = loss
    tail = sorted_returns[:idx]
    cvar = -sum(tail) / len(tail) if tail else var

    return {
        "var": round(var, 4),
        "cvar": round(cvar, 4),
        "confidence": confidence,
        "var_pct": round(var * 100, 1),
        "cvar_pct": round(cvar * 100, 1),
    }


def monte_carlo_simulation(expected_return: float, volatility: float,
                           n_sims: int, n_days: int, seed: int) -> Dict:
    """
    Run Monte Carlo simulation of daily returns over given horizon.

    Args:
        expected_return: Expected annual return (decimal).
        volatility: Annual volatility (decimal).
        n_sims: Number of simulations.
        n_days: Trading days in horizon.
        seed: Random seed for reproducibility.

    Returns:
        Dict with simulation statistics, percentiles, and return distribution.
    """
    daily_mu = expected_return / n_days
    daily_sigma = volatility / math.sqrt(n_days)

    random.seed(seed)

    if HAS_NUMPY:
        np.random.seed(seed)
        daily_returns = np.random.normal(daily_mu, daily_sigma, (n_sims, n_days))
        cumulative = np.prod(1 + daily_returns, axis=1) - 1
        # Max drawdown per path
        cum_wealth = np.cumprod(1 + daily_returns, axis=1)
        running_max = np.maximum.accumulate(cum_wealth, axis=1)
        drawdowns = (cum_wealth - running_max) / running_max
        max_drawdowns = np.min(drawdowns, axis=1)

        final_returns = cumulative.tolist()
        max_dd_list = max_drawdowns.tolist()
    else:
        final_returns = []
        max_dd_list = []
        for _ in range(n_sims):
            cum = 1.0
            peak = 1.0
            worst_dd = 0.0
            for _ in range(n_days):
                r = random.gauss(daily_mu, daily_sigma)
                cum *= (1 + r)
                if cum > peak:
                    peak = cum
                dd = (cum - peak) / peak
                if dd < worst_dd:
                    worst_dd = dd
            final_returns.append(cum - 1)
            max_dd_list.append(worst_dd)

    sorted_returns = sorted(final_returns)
    sorted_dd = sorted(max_dd_list)
    n = len(sorted_returns)

    def pctile(data, p):
        k = int(len(data) * p)
        k = min(k, len(data) - 1)
        return data[k]

    mean_return = sum(final_returns) / n
    # Probability of loss
    prob_loss = sum(1 for r in final_returns if r < 0) / n

    # VaR at multiple confidence levels
    var_95 = calc_var_cvar(final_returns, 0.95)
    var_99 = calc_var_cvar(final_returns, 0.99)

    return {
        "n_simulations": n_sims,
        "horizon_days": n_days,
        "mean_return_pct": round(mean_return * 100, 1),
        "median_return_pct": round(pctile(sorted_returns, 0.50) * 100, 1),
        "std_dev_pct": round((sum((r - mean_return)**2 for r in final_returns) / (n-1))**0.5 * 100, 1),
        "probability_of_loss_pct": round(prob_loss * 100, 1),
        "percentiles": {
            "p1": round(pctile(sorted_returns, 0.01) * 100, 1),
            "p5": round(pctile(sorted_returns, 0.05) * 100, 1),
            "p10": round(pctile(sorted_returns, 0.10) * 100, 1),
            "p25": round(pctile(sorted_returns, 0.25) * 100, 1),
            "p50": round(pctile(sorted_returns, 0.50) * 100, 1),
            "p75": round(pctile(sorted_returns, 0.75) * 100, 1),
            "p90": round(pctile(sorted_returns, 0.90) * 100, 1),
            "p95": round(pctile(sorted_returns, 0.95) * 100, 1),
            "p99": round(pctile(sorted_returns, 0.99) * 100, 1),
        },
        "var_95": var_95,
        "var_99": var_99,
        "max_drawdown": {
            "mean_pct": round(sum(max_dd_list) / n * 100, 1),
            "median_pct": round(pctile(sorted_dd, 0.50) * 100, 1),
            "p5_pct": round(pctile(sorted_dd, 0.05) * 100, 1),
            "worst_pct": round(min(max_dd_list) * 100, 1),
        },
        "price_distribution": {
            "p5": round(CURRENT_PRICE * (1 + pctile(sorted_returns, 0.05)), 2),
            "p25": round(CURRENT_PRICE * (1 + pctile(sorted_returns, 0.25)), 2),
            "p50": round(CURRENT_PRICE * (1 + pctile(sorted_returns, 0.50)), 2),
            "p75": round(CURRENT_PRICE * (1 + pctile(sorted_returns, 0.75)), 2),
            "p95": round(CURRENT_PRICE * (1 + pctile(sorted_returns, 0.95)), 2),
        },
    }


def drawdown_probability(volatility: float, thresholds: List[float],
                         horizon_years: float = 1.0) -> List[Dict]:
    """
    Estimate probability of hitting various drawdown levels within horizon.

    Args:
        volatility: Annual volatility (decimal).
        thresholds: List of drawdown levels to test (decimals, e.g., 0.20 = 20%).
        horizon_years: Time horizon in years.

    Returns:
        List of dicts with threshold and estimated probability.
    """
    # Using reflection principle approximation for geometric Brownian motion
    results = []
    for dd in thresholds:
        # Simplified: P(max drawdown > dd) ~ 2 * N(-dd / (vol * sqrt(T)))
        z = dd / (volatility * math.sqrt(horizon_years))
        # Normal CDF approximation
        prob = 2 * (1 - _norm_cdf(z))
        prob = min(prob, 1.0)
        results.append({
            "drawdown_threshold_pct": round(dd * 100, 0),
            "probability_pct": round(prob * 100, 1),
        })
    return results


def _norm_cdf(x: float) -> float:
    """Approximate standard normal CDF using error function."""
    return 0.5 * (1 + math.erf(x / math.sqrt(2)))


def risk_adjusted_returns(expected_return: float, volatility: float,
                          risk_free: float) -> Dict:
    """
    Calculate risk-adjusted return metrics.

    Args:
        expected_return: Expected annual return (decimal).
        volatility: Annual volatility (decimal).
        risk_free: Risk-free rate (decimal).

    Returns:
        Dict with Sharpe, Sortino (approx), Kelly fraction.
    """
    sharpe = (expected_return - risk_free) / volatility if volatility > 0 else 0
    # Sortino approximation: assume downside deviation ~ 70% of total vol for high-beta stocks
    downside_dev = volatility * 0.70
    sortino = (expected_return - risk_free) / downside_dev if downside_dev > 0 else 0
    # Kelly criterion: f* = (mu - rf) / sigma^2
    kelly = (expected_return - risk_free) / (volatility ** 2) if volatility > 0 else 0
    half_kelly = kelly / 2
    quarter_kelly = kelly / 4

    return {
        "sharpe_ratio": round(sharpe, 2),
        "sortino_ratio": round(sortino, 2),
        "kelly_full_pct": round(kelly * 100, 1),
        "kelly_half_pct": round(half_kelly * 100, 1),
        "kelly_quarter_pct": round(quarter_kelly * 100, 1),
    }


