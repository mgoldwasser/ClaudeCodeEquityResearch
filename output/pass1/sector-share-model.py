def project_cloud_shares(scenario="base"):
    """
    Projects market share evolution based on competitive dynamics.

    Key share shift drivers:
    1. AI workload capture (Azure advantage from OpenAI, AWS from custom silicon)
    2. Enterprise hybrid cloud preference (Azure advantage from Windows/M365 integration)
    3. Multi-cloud adoption (benefits GCP as #3 option)
    4. Regulatory data sovereignty (benefits all with regional presence)
    5. Price competition (benefits AWS scale, neoclouds for AI)
    6. Open-source AI (benefits neoclouds, pressures Azure premium)
    """

    # Share shift rates (pp per year) by scenario
    shifts = {
        "bull": {
            "AWS": -0.8,      # Losing share to Azure AI + neoclouds
            "Azure": +1.5,    # OpenAI integration + M365 Copilot flywheel
            "GCP": +0.5,      # Gemini AI + BigQuery analytics
            "Oracle": +0.4,   # OCI AI workloads + database moat
            "Alibaba": -0.2,  # Geopolitical limits growth
            "IBM": -0.3,      # Continued decline
            "Neoclouds": +0.5,# AI-specialized workloads
            "Other": -1.6     # Consolidation accelerates
        },
        "base": {
            "AWS": -0.5,
            "Azure": +1.0,
            "GCP": +0.3,
            "Oracle": +0.3,
            "Alibaba": -0.1,
            "IBM": -0.2,
            "Neoclouds": +0.3,
            "Other": -1.1
        },
        "bear": {
            "AWS": -0.2,      # Stabilizes with custom silicon + price leadership
            "Azure": +0.4,    # Slower gains; open-source AI limits premium
            "GCP": +0.2,
            "Oracle": +0.2,
            "Alibaba": -0.1,
            "IBM": -0.1,
            "Neoclouds": +0.1,
            "Other": -0.5
        }
    }

    shift = shifts[scenario]
    evolution = {provider: [share] for provider, share in CLOUD_SHARES_2025.items()}

    for year_idx in range(5):
        for provider in evolution:
            # Apply shift with slight decay (harder to gain share as you get bigger)
            adj_shift = shift[provider]
            if shift[provider] > 0:
                # Harder to gain share as you get bigger
                current = evolution[provider][-1]
                adj_shift *= (1 - current / 100 * 0.3)
            new_share = max(0.0, evolution[provider][-1] + adj_shift)
            evolution[provider].append(round(new_share, 1))

    # Normalize to 100%
    for year_idx in range(6):
        total = sum(evolution[p][year_idx] for p in evolution)
        for provider in evolution:
            evolution[provider][year_idx] = round(evolution[provider][year_idx] / total * 100, 1)

    return evolution


def compute_revenue_by_provider(shares, market_scenario="base"):
    """
    Calculates revenue ($B) by provider using market size forecast + share evolution.
    """
    market = CLOUD_MARKET_FORECASTS[market_scenario]
    revenue = {}

    for provider, share_list in shares.items():
        rev_list = []
        for year_idx in range(6):
            rev = market[year_idx] * share_list[year_idx] / 100
            rev_list.append(round(rev, 1))
        revenue[provider] = rev_list

    return revenue


