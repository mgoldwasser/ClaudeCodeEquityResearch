def monte_carlo_cloud_market(n_sims=10000, seed=42):
    """
    Monte Carlo simulation of cloud infrastructure market size in 2030.
    Uses normally distributed annual growth rates with mean/std from scenario analysis.
    """
    random.seed(seed)

    # Growth rate distribution parameters
    mean_growth = 0.18  # Base case 18% CAGR
    std_growth = 0.04   # Std dev captures bull/bear range

    terminal_values = []
    for _ in range(n_sims):
        market = CLOUD_INFRA_2025_B
        for year in range(5):
            # Growth rate is normally distributed but floored at 5%
            growth = max(0.05, random.gauss(mean_growth, std_growth))
            # Add slight deceleration bias (0.5pp per year)
            growth -= 0.005 * year
            market *= (1 + growth)
        terminal_values.append(market)

    terminal_values.sort()

    return {
        "n_simulations": n_sims,
        "mean_B": round(sum(terminal_values) / n_sims, 1),
        "median_B": round(terminal_values[n_sims // 2], 1),
        "p10_B": round(terminal_values[int(n_sims * 0.10)], 1),
        "p25_B": round(terminal_values[int(n_sims * 0.25)], 1),
        "p75_B": round(terminal_values[int(n_sims * 0.75)], 1),
        "p90_B": round(terminal_values[int(n_sims * 0.90)], 1),
        "min_B": round(terminal_values[0], 1),
        "max_B": round(terminal_values[-1], 1)
    }


