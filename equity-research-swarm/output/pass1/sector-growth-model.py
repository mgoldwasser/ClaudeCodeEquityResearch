    shares_2025 = {"AWS": 31.0, "Azure": 23.0, "GCP": 12.0, "Others": 34.0}

    # Annual share shift assumptions (pp per year)
    shift_scenarios = {
        "bull": {  # Azure gains most from AI positioning
            "AWS": -0.8,
            "Azure": +1.5,
            "GCP": +0.5,
            "Others": -1.2
        },
        "base": {  # Gradual Azure gains, GCP holds
            "AWS": -0.5,
            "Azure": +1.0,
            "GCP": +0.3,
            "Others": -0.8
        },
        "bear": {  # Share shifts slow, AWS stabilizes
            "AWS": -0.3,
            "Azure": +0.5,
            "GCP": +0.2,
            "Others": -0.4
        }
    }

    shifts = shift_scenarios[scenario]
    evolution = {provider: [share] for provider, share in shares_2025.items()}

    for year in range(5):
        for provider in evolution:
            new_share = evolution[provider][-1] + shifts[provider]
            evolution[provider].append(round(new_share, 1))

    return {
        "scenario": scenario,
        "years": list(range(2025, 2031)),
        "shares": evolution,
        "azure_2030": evolution["Azure"][-1],
        "aws_2030": evolution["AWS"][-1]
    }


