def calc_leverage_ratios(total_debt: float, net_debt: float, ebitda: float,
                         equity: float, total_capital: float) -> Dict:
    """
    Calculate comprehensive leverage ratios.

    Args:
        total_debt: Total debt ($M)
        net_debt: Net debt ($M)
        ebitda: EBITDA ($M)
        equity: Total stockholders' equity ($M)
        total_capital: Total debt + equity ($M)

    Returns:
        Dict with all leverage metrics
    """
    return {
        'net_debt_ebitda': net_debt / ebitda if ebitda > 0 else float('inf'),
        'total_debt_ebitda': total_debt / ebitda if ebitda > 0 else float('inf'),
        'debt_equity': total_debt / equity if equity > 0 else float('inf'),
        'net_debt_equity': net_debt / equity if equity > 0 else float('inf'),
        'debt_total_capital': total_debt / total_capital if total_capital > 0 else 0,
        'net_debt_total_capital': net_debt / total_capital if total_capital > 0 else 0,
        'equity_ratio': equity / (total_debt + equity) if (total_debt + equity) > 0 else 0,
    }


def calc_coverage_ratios(ebitda: float, interest: float, capex: float,
                         ocf: float, total_debt: float,
                         total_debt_service: float) -> Dict:
    """
    Calculate interest and debt service coverage ratios.

    Args:
        ebitda: EBITDA ($M)
        interest: Interest expense ($M)
        capex: Capital expenditures ($M)
        ocf: Operating cash flow ($M)
        total_debt: Total debt ($M)
        total_debt_service: Annual debt maturities + interest ($M)

    Returns:
        Dict with all coverage metrics
    """
    ebitda_less_capex = ebitda - capex

    return {
        'ebitda_interest': ebitda / interest if interest > 0 else float('inf'),
        'ebitda_less_capex_interest': ebitda_less_capex / interest if interest > 0 else float('inf'),
        'ocf_total_debt': ocf / total_debt if total_debt > 0 else float('inf'),
        'fcf_total_debt_service': (ocf - capex) / total_debt_service if total_debt_service > 0 else float('inf'),
        'ocf_interest': ocf / interest if interest > 0 else float('inf'),
    }


def calc_fcf_after_debt_service(ocf: float, capex: float, interest: float,
                                 debt_maturities: float, dividends: float,
                                 buybacks: float) -> Dict:
    """
    Calculate free cash flow waterfall after all obligations.

    Args:
        ocf: Operating cash flow ($M)
        capex: Capital expenditures ($M)
        interest: Interest expense ($M)
        debt_maturities: Near-term debt maturities ($M)
        dividends: Annual dividends ($M)
        buybacks: Annual share buybacks ($M)

    Returns:
        Dict with FCF waterfall
    """
    fcf = ocf - capex
    fcf_after_interest = fcf - interest
    fcf_after_debt_service = fcf_after_interest - debt_maturities
    fcf_after_dividends = fcf_after_debt_service - dividends
    fcf_after_all = fcf_after_dividends - buybacks

    return {
        'operating_cash_flow': ocf,
        'minus_capex': capex,
        'free_cash_flow': fcf,
        'minus_interest': interest,
        'fcf_after_interest': fcf_after_interest,
        'minus_debt_maturities': debt_maturities,
        'fcf_after_debt_service': fcf_after_debt_service,
        'minus_dividends': dividends,
        'fcf_after_dividends': fcf_after_dividends,
        'minus_buybacks': buybacks,
        'fcf_after_all_obligations': fcf_after_all,
        'ocf_coverage_of_all': ocf / (capex + interest + debt_maturities + dividends + buybacks) if (capex + interest + debt_maturities + dividends + buybacks) > 0 else float('inf'),
    }


def analyze_debt_maturity(schedule: Dict, total_debt: float,
                          available_liquidity: float) -> Dict:
    """
    Analyze debt maturity profile and refinancing risk.

    Args:
        schedule: Maturity schedule by year
        total_debt: Total outstanding debt ($M)
        available_liquidity: Total available liquidity ($M)

    Returns:
        Dict with maturity analysis
    """
    results = []
    cumulative = 0

    for year, components in sorted(schedule.items()):
        total_year = sum(v for k, v in components.items() if k != 'label')
        cumulative += total_year
        pct_of_total = (total_year / total_debt * 100) if total_debt > 0 else 0
        cum_pct = (cumulative / total_debt * 100) if total_debt > 0 else 0

        results.append({
            'year': year,
            'label': components['label'],
            'bonds': components.get('bonds', 0),
            'finance_leases': components.get('finance_leases', 0),
            'commercial_paper': components.get('commercial_paper', 0),
            'total': total_year,
            'cumulative': cumulative,
            'pct_of_total': pct_of_total,
            'cumulative_pct': cum_pct,
        })

    # Near-term maturity wall (next 2 years)
    near_term = sum(r['total'] for r in results if r['year'] <= 2027)
    near_term_coverage = available_liquidity / near_term if near_term > 0 else float('inf')

    # Weighted average maturity (simplified)
    total_weighted = 0
    total_amount = 0
    base_year = 2026
    for r in results:
        years_out = r['year'] - base_year + 0.5
        if r['label'].endswith('+'):
            years_out = 10  # Approximate for 2031+ bucket
        total_weighted += r['total'] * years_out
        total_amount += r['total']
    weighted_avg_maturity = total_weighted / total_amount if total_amount > 0 else 0

    return {
        'schedule': results,
        'near_term_maturities_2yr': near_term,
        'near_term_coverage': near_term_coverage,
        'weighted_avg_maturity_years': weighted_avg_maturity,
        'total_scheduled': cumulative,
    }


def capex_sustainability(ocf_current: float, capex_current: float,
                          ocf_growth_rate: float, capex_trajectory: List[float],
                          shareholder_returns: float) -> Dict:
    """
    Analyze CapEx sustainability and years until FCF normalizes.

    Args:
        ocf_current: Current operating cash flow ($M)
        capex_current: Current CapEx ($M)
        ocf_growth_rate: Expected OCF growth rate (decimal)
        capex_trajectory: Projected CapEx for next 5 years ($M)
        shareholder_returns: Annual dividends + buybacks ($M)

    Returns:
        Dict with CapEx sustainability metrics
    """
    capex_to_ocf = capex_current / ocf_current if ocf_current > 0 else float('inf')
    fcf = ocf_current - capex_current
    fcf_margin = fcf / ocf_current if ocf_current > 0 else 0

    # Project FCF over 5 years
    projections = []
    ocf = ocf_current
    normalize_year = None

    for i, capex in enumerate(capex_trajectory):
        year = 2026 + i
        ocf = ocf * (1 + ocf_growth_rate) if i > 0 else ocf_current
        fcf_yr = ocf - capex
        fcf_after_returns = fcf_yr - shareholder_returns
        fcf_margin_yr = fcf_yr / ocf if ocf > 0 else 0

        projections.append({
            'year': f'FY{year}',
            'ocf': ocf,
            'capex': capex,
            'fcf': fcf_yr,
            'fcf_after_returns': fcf_after_returns,
            'capex_to_ocf_pct': (capex / ocf * 100) if ocf > 0 else 0,
            'fcf_margin_pct': fcf_margin_yr * 100,
        })

        # Normalize = CapEx/OCF drops below 50% AND FCF after returns > $20B
        if normalize_year is None and (capex / ocf < 0.50) and fcf_after_returns > 20000:
            normalize_year = year

    return {
        'current_capex_to_ocf_pct': capex_to_ocf * 100,
        'current_fcf': fcf,
        'current_fcf_margin_pct': fcf_margin * 100,
        'projections': projections,
        'normalize_year': normalize_year,
        'years_to_normalize': (normalize_year - 2026) if normalize_year else None,
    }


def calc_altman_z_score(working_capital: float, total_assets: float,
                         retained_earnings: float, ebit: float,
                         market_cap: float, total_liabilities: float,
                         sales: float) -> Dict:
    """
    Calculate Altman Z-Score for bankruptcy risk assessment.

    Formula: Z = 1.2*X1 + 1.4*X2 + 3.3*X3 + 0.6*X4 + 1.0*X5
    Where:
        X1 = Working Capital / Total Assets
        X2 = Retained Earnings / Total Assets
        X3 = EBIT / Total Assets
        X4 = Market Value of Equity / Total Liabilities
        X5 = Sales / Total Assets

    Args:
        working_capital: Current Assets - Current Liabilities ($M)
        total_assets: Total assets ($M)
        retained_earnings: Retained earnings ($M)
        ebit: Earnings before interest and taxes ($M)
        market_cap: Market capitalization ($M)
        total_liabilities: Total liabilities ($M)
        sales: Total revenue ($M)

    Returns:
        Dict with Z-Score components and interpretation
    """
    x1 = working_capital / total_assets if total_assets > 0 else 0
    x2 = retained_earnings / total_assets if total_assets > 0 else 0
    x3 = ebit / total_assets if total_assets > 0 else 0
    x4 = market_cap / total_liabilities if total_liabilities > 0 else 0
    x5 = sales / total_assets if total_assets > 0 else 0

    z_score = 1.2 * x1 + 1.4 * x2 + 3.3 * x3 + 0.6 * x4 + 1.0 * x5

    # Interpretation
    if z_score > 2.99:
        zone = 'Safe Zone'
        risk = 'Negligible bankruptcy risk'
    elif z_score > 1.81:
        zone = 'Grey Zone'
        risk = 'Moderate bankruptcy risk — monitor'
    else:
        zone = 'Distress Zone'
        risk = 'High bankruptcy risk'

    return {
        'z_score': z_score,
        'zone': zone,
        'risk_assessment': risk,
        'components': {
            'X1_wc_ta': {'value': x1, 'weighted': 1.2 * x1,
                         'label': 'Working Capital / Total Assets'},
            'X2_re_ta': {'value': x2, 'weighted': 1.4 * x2,
                         'label': 'Retained Earnings / Total Assets'},
            'X3_ebit_ta': {'value': x3, 'weighted': 3.3 * x3,
                           'label': 'EBIT / Total Assets'},
            'X4_mve_tl': {'value': x4, 'weighted': 0.6 * x4,
                          'label': 'Market Cap / Total Liabilities'},
            'X5_sales_ta': {'value': x5, 'weighted': 1.0 * x5,
                            'label': 'Sales / Total Assets'},
        },
        'thresholds': {
            'safe': '> 2.99',
            'grey': '1.81 - 2.99',
            'distress': '< 1.81'
        }
    }


def run_credit_stress_test(revenue_base: float, ebitda_margin_base: float,
                            margin_delta_per_pct: float, base_growth: float,
                            growth_scenarios: List[float], capex: float,
                            interest: float, total_debt: float,
                            cash: float, shareholder_returns: float) -> Dict:
    """
    Stress test credit metrics across revenue growth scenarios.

    Args:
        revenue_base: Base year revenue ($M)
        ebitda_margin_base: EBITDA margin at base growth rate
        margin_delta_per_pct: Margin compression per pct below base growth
        base_growth: Base revenue growth rate (decimal)
        growth_scenarios: List of growth rate scenarios (decimal)
        capex: CapEx held constant ($M)
        interest: Annual interest expense ($M)
        total_debt: Total debt ($M)
        cash: Cash + investments ($M)
        shareholder_returns: Annual dividends + buybacks ($M)

    Returns:
        Dict with stress test results for each scenario
    """
    results = []

    for growth in growth_scenarios:
        # Project forward 1 year
        revenue_fwd = revenue_base * (1 + growth)

        # Margin adjusts based on growth deviation from base
        growth_delta = base_growth - growth  # positive = slower growth
        margin_compression = growth_delta * margin_delta_per_pct / 0.01
        ebitda_margin = max(0.30, ebitda_margin_base - margin_compression)

        ebitda_fwd = revenue_fwd * ebitda_margin
        # OCF approximation: EBITDA - tax on operating income + WC benefit
        ocf_fwd = ebitda_fwd * 0.85  # [ASSUMPTION: OCF/EBITDA ~85%]
        fcf_fwd = ocf_fwd - capex

        # Credit metrics
        net_debt_fwd = total_debt - cash  # Simplified: cash unchanged
        if fcf_fwd < shareholder_returns:
            # Deficit funded by additional debt
            deficit = shareholder_returns - fcf_fwd
            net_debt_fwd += deficit
            new_debt_needed = deficit
        else:
            new_debt_needed = 0

        net_debt_ebitda = net_debt_fwd / ebitda_fwd if ebitda_fwd > 0 else float('inf')
        interest_coverage = ebitda_fwd / interest if interest > 0 else float('inf')
        ebitda_less_capex_coverage = (ebitda_fwd - capex) / interest if interest > 0 else float('inf')
        fcf_after_returns = fcf_fwd - shareholder_returns

        # Determine implied rating
        implied_rating = 'Below A'
        for rating, thresholds in sorted(RATING_THRESHOLDS.items()):
            if (net_debt_ebitda <= thresholds['net_debt_ebitda_max'] and
                interest_coverage >= thresholds['interest_coverage_min']):
                implied_rating = rating
                break

        results.append({
            'revenue_growth_pct': growth * 100,
            'revenue_fwd': revenue_fwd,
            'ebitda_margin_pct': ebitda_margin * 100,
            'ebitda_fwd': ebitda_fwd,
            'ocf_fwd': ocf_fwd,
            'fcf_fwd': fcf_fwd,
            'net_debt_fwd': net_debt_fwd,
            'net_debt_ebitda': net_debt_ebitda,
            'interest_coverage': interest_coverage,
            'ebitda_less_capex_coverage': ebitda_less_capex_coverage,
            'fcf_after_returns': fcf_after_returns,
            'new_debt_needed': new_debt_needed,
            'implied_rating': implied_rating,
        })

    return {'scenarios': results}


def format_number(n, decimals=0):
    """Format number with commas and optional decimals."""
    if n is None:
        return "N/A"
    if isinstance(n, float) and (n == float('inf') or n == float('-inf')):
        return "N/M"
    if decimals == 0:
        return f"{n:,.0f}"
    return f"{n:,.{decimals}f}"


