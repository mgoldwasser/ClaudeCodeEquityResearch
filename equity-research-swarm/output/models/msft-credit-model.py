    print(f"  {'':>6} {'':>40} {'':>8} {'--------':>10}")
    print(f"  {'':>6} {'Z-Score':<40} {'':>8} {z_result['z_score']:>10.2f}")
    print(f"\n  Thresholds: Safe > 2.99 | Grey 1.81-2.99 | Distress < 1.81")

