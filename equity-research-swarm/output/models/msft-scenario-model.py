def calc_scenario_returns(current_price: float, scenarios: List[Dict]) -> List[Dict]:
    """
    Calculate return metrics for each scenario.

    Args:
        current_price: Current stock price
        scenarios: List of scenario dicts with 'price' and 'probability'

    Returns:
        List of enriched scenario dicts with return calculations
    """
    results = []
    for s in scenarios:
        target = s['price']
        prob = s['probability']
        dollar_return = target - current_price
        pct_return = (target / current_price - 1)
        weighted_return = prob * pct_return
        weighted_price = prob * target

        results.append({
            **s,
            'dollar_return': dollar_return,
            'pct_return': pct_return,
            'pct_return_display': pct_return * 100,
            'weighted_return': weighted_return,
            'weighted_price': weighted_price,
        })

    return results


def calc_expected_value(scenarios: List[Dict]) -> Dict:
    """
    Calculate probability-weighted expected value.

    Args:
        scenarios: List of scenario dicts with 'price', 'probability', 'pct_return'

    Returns:
        Dict with expected price, return, and risk metrics
    """
    ev_price = sum(s['probability'] * s['price'] for s in scenarios)
    ev_return = sum(s['probability'] * s['pct_return'] for s in scenarios)

    # Variance and standard deviation of returns
    variance = sum(
        s['probability'] * (s['pct_return'] - ev_return) ** 2
        for s in scenarios
    )
    std_dev = math.sqrt(variance)

    # Probability of positive return
    prob_positive = sum(
        s['probability'] for s in scenarios
        if s['pct_return'] > 0
    )

    # Upside/downside ratio (probability-weighted)
    upside = sum(
        s['probability'] * s['pct_return']
        for s in scenarios if s['pct_return'] > 0
    )
    downside = abs(sum(
        s['probability'] * s['pct_return']
        for s in scenarios if s['pct_return'] < 0
    ))
    upside_downside_ratio = upside / downside if downside > 0 else float('inf')

    return {
        'expected_price': ev_price,
        'expected_return_pct': ev_return * 100,
        'return_std_dev_pct': std_dev * 100,
        'probability_positive': prob_positive * 100,
        'upside_downside_ratio': upside_downside_ratio,
        'implied_upside_from_current': (ev_price / CURRENT_PRICE - 1) * 100,
    }


def calc_kelly_criterion(scenarios: List[Dict], risk_free_rate: float) -> Dict:
    """
    Calculate Kelly criterion for position sizing.

    Kelly formula for multiple discrete outcomes:
        f* = sum(p_i * b_i) / sum(p_i * b_i^2)
    where b_i = (price_i / current - 1) = return for scenario i,
    and p_i = probability of scenario i.

    Simplified approach: Kelly = (expected_excess_return) / (variance_of_returns)
    This is the continuous approximation used for practical position sizing.

    Args:
        scenarios: List of scenario dicts
        risk_free_rate: Annual risk-free rate

    Returns:
        Dict with Kelly fractions and position sizing
    """
    # Expected return
    ev_return = sum(s['probability'] * s['pct_return'] for s in scenarios)
    excess_return = ev_return - risk_free_rate

    # Variance of returns
    variance = sum(
        s['probability'] * (s['pct_return'] - ev_return) ** 2
        for s in scenarios
    )

    # Kelly fraction: f* = excess_return / variance
    if variance > 0:
        kelly_full = excess_return / variance
    else:
        kelly_full = 0

    kelly_half = kelly_full / 2
    kelly_quarter = kelly_full / 4

    # Clamp to reasonable bounds
    kelly_full_clamped = max(0, min(kelly_full, 2.0))
    kelly_half_clamped = max(0, min(kelly_half, 1.0))
    kelly_quarter_clamped = max(0, min(kelly_quarter, 0.5))

    # Position sizing in dollars (assuming $10M portfolio)
    portfolio_size = 10_000_000
    position_full = portfolio_size * kelly_full_clamped
    position_half = portfolio_size * kelly_half_clamped
    position_quarter = portfolio_size * kelly_quarter_clamped

    return {
        'expected_return_pct': ev_return * 100,
        'excess_return_pct': excess_return * 100,
        'return_variance': variance,
        'return_std_dev_pct': math.sqrt(variance) * 100,
        'kelly_full': kelly_full,
        'kelly_full_pct': kelly_full * 100,
        'kelly_half': kelly_half,
        'kelly_half_pct': kelly_half * 100,
        'kelly_quarter': kelly_quarter,
        'kelly_quarter_pct': kelly_quarter * 100,
        'kelly_full_clamped_pct': kelly_full_clamped * 100,
        'kelly_half_clamped_pct': kelly_half_clamped * 100,
        'kelly_quarter_clamped_pct': kelly_quarter_clamped * 100,
        'position_full_10m': position_full,
        'position_half_10m': position_half,
        'position_quarter_10m': position_quarter,
    }


def calc_breakeven_bear_probability(bull_price: float, base_price: float,
                                     bear_price: float, current_price: float,
                                     bull_prob: float) -> Dict:
    """
    Calculate the bear case probability at which expected value equals current price
    (i.e., the break-even probability for the bear scenario).

    Holding bull probability constant, solve for bear probability where EV = current price.
    base_prob = 1 - bull_prob - bear_prob

    EV = bull_prob * bull_price + (1 - bull_prob - bear_prob) * base_price + bear_prob * bear_price
    Set EV = current_price and solve for bear_prob.

    Args:
        bull_price, base_price, bear_price: Scenario prices
        current_price: Current stock price
        bull_prob: Fixed bull probability

    Returns:
        Dict with breakeven bear probability
    """
    # EV = bull_prob*bull + (1-bull_prob-bear_prob)*base + bear_prob*bear = current
    # bull_prob*bull + base - bull_prob*base - bear_prob*base + bear_prob*bear = current
    # bear_prob*(bear - base) = current - bull_prob*bull - base + bull_prob*base
    # bear_prob = (current - bull_prob*bull - base*(1-bull_prob)) / (bear - base)

    numerator = current_price - bull_prob * bull_price - base_price * (1 - bull_prob)
    denominator = bear_price - base_price

    if denominator != 0:
        breakeven_bear = numerator / denominator
    else:
        breakeven_bear = 0

    breakeven_bear = max(0, min(breakeven_bear, 1 - bull_prob))

    return {
        'breakeven_bear_probability': breakeven_bear,
        'breakeven_bear_probability_pct': breakeven_bear * 100,
        'current_bear_probability_pct': BEAR_PROBABILITY * 100,
        'margin_of_safety': (breakeven_bear - BEAR_PROBABILITY) * 100,
        'interpretation': (
            f"Bear probability would need to reach {breakeven_bear*100:.1f}% "
            f"(vs current {BEAR_PROBABILITY*100:.0f}%) for EV to equal current price. "
            f"Margin of safety: {(breakeven_bear - BEAR_PROBABILITY)*100:.1f} percentage points."
        ),
    }


