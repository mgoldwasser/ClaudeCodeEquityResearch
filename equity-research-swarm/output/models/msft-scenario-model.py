def format_number(n, decimals=0):
    """Format number with commas and optional decimals."""
    if n is None:
        return "N/A"
    if isinstance(n, float) and (n == float('inf') or n == float('-inf')):
        return "N/M"
    if decimals == 0:
        return f"{n:,.0f}"
    return f"{n:,.{decimals}f}"


