def build_segment_revenue(cloud_base: float, prod_base: float, pc_base: float,
                          cloud_growth: List[float], prod_growth: List[float],
                          pc_growth: List[float], years: int = 5) -> Dict:
    """
    Build 5-year segment-level revenue projection.

    Args:
        cloud_base: Base year Intelligent Cloud revenue ($M)
        prod_base: Base year Productivity & Business revenue ($M)
        pc_base: Base year Personal Computing revenue ($M)
        cloud_growth: Annual growth rates for cloud segment
        prod_growth: Annual growth rates for productivity segment
        pc_growth: Annual growth rates for personal computing segment
        years: Number of projection years

    Returns:
        Dict with 'cloud', 'prod', 'pc', 'total' revenue arrays
    """
    cloud = [cloud_base]
    prod = [prod_base]
    pc = [pc_base]
    total = [cloud_base + prod_base + pc_base]

    for i in range(years):
        cloud.append(cloud[-1] * (1 + cloud_growth[i]))
        prod.append(prod[-1] * (1 + prod_growth[i]))
        pc.append(pc[-1] * (1 + pc_growth[i]))
        total.append(cloud[-1] + prod[-1] + pc[-1])

    return {
        'cloud': cloud,
        'prod': prod,
        'pc': pc,
        'total': total,
        'years': ['FY2025'] + [f'FY{2026 + i}' for i in range(years)]
    }


def build_income_statement(revenue: List[float], gross_margins: List[float],
                           opex_pcts: List[float]) -> Dict:
    """
    Build projected income statement from revenue.

    Args:
        revenue: Total revenue by year including base year ($M)
        gross_margins: Gross margin percentages for projection years
        opex_pcts: OpEx as % of revenue for projection years

    Returns:
        Dict with gross_profit, opex, operating_income, ebitda arrays
    """
    # Base year actuals
    gp = [FY2025_GROSS_PROFIT]
    opex = [FY2025_GROSS_PROFIT - FY2025_OPERATING_INCOME]
    oi = [FY2025_OPERATING_INCOME]

    for i in range(len(gross_margins)):
        yr_rev = revenue[i + 1]
        yr_gp = yr_rev * gross_margins[i]
        yr_opex = yr_rev * opex_pcts[i]
        yr_oi = yr_gp - yr_opex

        gp.append(yr_gp)
        opex.append(yr_opex)
        oi.append(yr_oi)

    return {
        'gross_profit': gp,
        'opex': opex,
        'operating_income': oi
    }


def build_fcf(operating_income: List[float], da: List[float],
              capex: List[float], revenue: List[float],
              tax_rate: float, wc_pct: float) -> Dict:
    """
    Build free cash flow bridge.

    Args:
        operating_income: Operating income by year ($M)
        da: Depreciation & amortization by year ($M)
        capex: Capital expenditures by year ($M)
        revenue: Revenue by year ($M)
        tax_rate: Effective tax rate
        wc_pct: Working capital change as % of revenue change

    Returns:
        Dict with ebitda, nopat, fcf arrays
    """
    ebitda = [operating_income[0] + FY2025_DA]  # base year
    nopat = [operating_income[0] * (1 - tax_rate)]
    wc_change = [0]
    fcf = [nopat[0] + FY2025_DA - FY2025_CAPEX - 0]  # base year FCF

    for i in range(len(da)):
        yr_ebitda = operating_income[i + 1] + da[i]
        yr_nopat = operating_income[i + 1] * (1 - tax_rate)
        yr_rev_change = revenue[i + 1] - revenue[i]
        yr_wc = yr_rev_change * wc_pct
        yr_fcf = yr_nopat + da[i] - capex[i] - yr_wc

        ebitda.append(yr_ebitda)
        nopat.append(yr_nopat)
        wc_change.append(yr_wc)
        fcf.append(yr_fcf)

    return {
        'ebitda': ebitda,
        'nopat': nopat,
        'wc_change': wc_change,
        'fcf': fcf
    }


def calc_wacc(rf: float, erp: float, beta: float, size_prem: float,
              specific: float, pre_tax_debt_cost: float, tax_rate: float,
              debt: float, equity: float) -> float:
    """
    Calculate Weighted Average Cost of Capital.

    Args:
        rf: Risk-free rate
        erp: Equity risk premium
        beta: Equity beta
        size_prem: Size premium
        specific: Company-specific risk premium
        pre_tax_debt_cost: Pre-tax cost of debt
        tax_rate: Marginal tax rate for debt shield
        debt: Total debt ($M)
        equity: Market cap ($M)

    Returns:
        WACC as decimal
    """
    cost_of_equity = rf + (beta * erp) + size_prem + specific
    after_tax_debt_cost = pre_tax_debt_cost * (1 - tax_rate)
    total_capital = debt + equity
    weight_equity = equity / total_capital
    weight_debt = debt / total_capital
    wacc = (weight_equity * cost_of_equity) + (weight_debt * after_tax_debt_cost)
    return wacc


def calc_terminal_value(final_fcf: float, wacc: float, tgr: float,
                        final_ebitda: float, exit_mult: float) -> Dict:
    """
    Calculate terminal value using both perpetuity growth and exit multiple methods.

    Args:
        final_fcf: Final year free cash flow ($M)
        wacc: Weighted average cost of capital
        tgr: Terminal growth rate
        final_ebitda: Final year EBITDA ($M)
        exit_mult: Exit EV/EBITDA multiple

    Returns:
        Dict with perpetuity_tv, exit_tv, blended_tv (50/50 weight)
    """
    if wacc <= tgr:
        raise ValueError(f"WACC ({wacc:.4f}) must exceed TGR ({tgr:.4f})")

    perpetuity_tv = (final_fcf * (1 + tgr)) / (wacc - tgr)
    exit_tv = final_ebitda * exit_mult
    blended_tv = (perpetuity_tv * 0.50) + (exit_tv * 0.50)

    return {
        'perpetuity_tv': perpetuity_tv,
        'exit_tv': exit_tv,
        'blended_tv': blended_tv,
        'perpetuity_weight': 0.50,
        'exit_weight': 0.50
    }


def calc_enterprise_value(fcfs: List[float], terminal_value: float,
                          wacc: float) -> Dict:
    """
    Discount FCFs and terminal value to present value.

    Args:
        fcfs: List of projected free cash flows (years 1-5) ($M)
        terminal_value: Terminal value at end of year 5 ($M)
        wacc: Weighted average cost of capital

    Returns:
        Dict with pv_fcfs, pv_tv, enterprise_value, tv_pct_of_ev
    """
    pv_fcfs = []
    for i, fcf in enumerate(fcfs):
        pv = fcf / ((1 + wacc) ** (i + 1))
        pv_fcfs.append(pv)

    pv_tv = terminal_value / ((1 + wacc) ** len(fcfs))
    ev = sum(pv_fcfs) + pv_tv

    tv_pct = pv_tv / ev * 100 if ev > 0 else 0

    return {
        'pv_fcfs': pv_fcfs,
        'pv_tv': pv_tv,
        'enterprise_value': ev,
        'tv_pct_of_ev': tv_pct
    }


def equity_bridge(ev: float, cash: float, debt: float,
                  minority: float, shares: float) -> Dict:
    """
    Bridge from enterprise value to equity value per share.

    Args:
        ev: Enterprise value ($M)
        cash: Cash and investments ($M)
        debt: Total debt ($M)
        minority: Minority interest ($M)
        shares: Diluted shares outstanding (millions)

    Returns:
        Dict with equity_value, price_per_share
    """
    equity_value = ev + cash - debt - minority
    price_per_share = equity_value / shares if shares > 0 else 0

    return {
        'enterprise_value': ev,
        'plus_cash': cash,
        'minus_debt': debt,
        'minus_minority': minority,
        'equity_value': equity_value,
        'shares': shares,
        'price_per_share': price_per_share
    }


def sensitivity_table(base_wacc: float, base_tgr: float,
                      final_fcf: float, final_ebitda: float,
                      exit_mult: float, fcfs: List[float],
                      cash: float, debt: float, minority: float,
                      shares: float) -> Dict:
    """
    Generate WACC vs Terminal Growth Rate sensitivity table.

    Args:
        base_wacc: Base WACC
        base_tgr: Base terminal growth rate
        final_fcf: Final year FCF ($M)
        final_ebitda: Final year EBITDA ($M)
        exit_mult: Exit multiple
        fcfs: Projected FCFs ($M)
        cash, debt, minority, shares: Bridge components

    Returns:
        Dict with 'table' (2D list), 'wacc_range', 'tgr_range'
    """
    wacc_range = [base_wacc + d for d in [-0.015, -0.010, -0.005, 0.0, 0.005, 0.010, 0.015]]
    tgr_range = [base_tgr + d for d in [-0.015, -0.010, -0.005, 0.0, 0.005, 0.010, 0.015]]

    table = []
    for w in wacc_range:
        row = []
        for t in tgr_range:
            if w <= t:
                row.append(None)
                continue
            try:
                tv = calc_terminal_value(final_fcf, w, t, final_ebitda, exit_mult)
                ev_data = calc_enterprise_value(fcfs, tv['blended_tv'], w)
                bridge = equity_bridge(ev_data['enterprise_value'], cash, debt, minority, shares)
                row.append(round(bridge['price_per_share'], 2))
            except (ValueError, ZeroDivisionError):
                row.append(None)
        table.append(row)

    return {
        'table': table,
        'wacc_range': [round(w * 100, 2) for w in wacc_range],
        'tgr_range': [round(t * 100, 2) for t in tgr_range]
    }


def run_scenario(scenario_name: str, cloud_growth: List[float],
                 prod_growth: List[float], pc_growth: List[float],
                 gross_margins: List[float], opex_pcts: List[float],
                 capex: List[float], da: List[float],
                 tgr: float, exit_mult: float, wacc: float) -> Dict:
    """
    Run a complete DCF scenario.

    Args:
        scenario_name: 'bull', 'base', or 'bear'
        cloud_growth, prod_growth, pc_growth: Segment growth rates
        gross_margins: Gross margin assumptions
        opex_pcts: OpEx % of revenue assumptions
        capex: CapEx assumptions ($M)
        da: D&A assumptions ($M)
        tgr: Terminal growth rate
        exit_mult: Exit EV/EBITDA multiple
        wacc: Weighted average cost of capital

    Returns:
        Dict with full scenario results
    """
    # Build revenue
    rev = build_segment_revenue(
        FY2025_INTELLIGENT_CLOUD, FY2025_PRODUCTIVITY_BIZ, FY2025_PERSONAL_COMPUTING,
        cloud_growth, prod_growth, pc_growth
    )

    # Build income statement
    inc = build_income_statement(rev['total'], gross_margins, opex_pcts)

    # Build FCF
    cashflow = build_fcf(
        inc['operating_income'], da, capex, rev['total'],
        EFFECTIVE_TAX_RATE, WC_CHANGE_PCT_OF_REVENUE_CHANGE
    )

    # Terminal value (using year 5 projections)
    tv = calc_terminal_value(
        cashflow['fcf'][-1], wacc, tgr,
        cashflow['ebitda'][-1], exit_mult
    )

    # Enterprise value (years 1-5 FCFs, excluding base year)
    ev_data = calc_enterprise_value(cashflow['fcf'][1:], tv['blended_tv'], wacc)

    # Equity bridge
    bridge = equity_bridge(
        ev_data['enterprise_value'],
        CASH_AND_INVESTMENTS, TOTAL_DEBT_BRIDGE, MINORITY_INTEREST, SHARES_OUTSTANDING
    )

    # Sensitivity table
    sens = sensitivity_table(
        wacc, tgr, cashflow['fcf'][-1], cashflow['ebitda'][-1],
        exit_mult, cashflow['fcf'][1:],
        CASH_AND_INVESTMENTS, TOTAL_DEBT_BRIDGE, MINORITY_INTEREST, SHARES_OUTSTANDING
    )

    return {
        'scenario': scenario_name,
        'revenue': rev,
        'income_statement': inc,
        'cash_flow': cashflow,
        'terminal_value': tv,
        'ev_data': ev_data,
        'bridge': bridge,
        'sensitivity': sens,
        'implied_price': bridge['price_per_share']
    }


def format_number(n, decimals=0):
    """Format number with commas and optional decimals."""
    if n is None:
        return "N/A"
    if decimals == 0:
        return f"{n:,.0f}"
    return f"{n:,.{decimals}f}"


